	<span id="sent257">When the Italian journalist Riccardo Ehrman, the Berlin correspondent of ANSA newsagency, asked when the regulations would come into effect, Schabowski assumed it would be the same day based on the wording of the note and replied ``As far as I know effective immediately, without delay''. </span>

	<span id="sent258">After further questions from journalists he confirmed that the regulations included the border crossings towards West Berlin, which he had not mentioned until then.<br /><br /></span>

