	<span id="sent266">Demolition.<br /><br /></span>

	<span id="sent267">November 9 is considered the date the Wall fell, but the Wall in its entirety was not torn down immediately. </span>

	<span id="sent268">Starting that evening, and in the days and weeks that followed, people came to the wall with sledgehammers or otherwise hammers and chisels to chip off souvenirs, demolishing lengthy parts of it in the process and creating several unofficial border crossings. </span>

	<span id="sent269">These people were nicknamed ``Mauerspechte'' (wall woodpeckers).<br /><br /></span>

