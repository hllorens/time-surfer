	<span id="sent241">The longtime leader of East Germany, Erich Honecker, resigned on October 18, 1989, and was replaced by Egon Krenz a few days later. </span>

	<span id="sent242">Honecker had predicted in January of that year that the wall would stand for a ``hundred more years'' if the conditions which had caused its construction did not change.<br /><br /></span>

	<span id="sent243">Protest demonstrations broke out all over East Germany in September 1989. </span>

	<span id="sent244">Initially, protesters were mostly people wanting to leave to the West, chanting ``Wir wollen raus!'' </span>

	<span id="sent245">(``We want out!''). </span>

	<span id="sent246">Then protestors began to chant ``Wir bleiben hier'', (``We're staying here!''). </span>

	<span id="sent247">This was the start of what East Germans generally call the `` Peaceful Revolution'' of late 1989. </span>

	<span id="sent248">By November 4, the protests had swelled significantly, with half a million people gathered that day at the Alexanderplatz demonstration in East Berlin (Henslin, 07).<br /><br /></span>

	<span id="sent249">Meanwhile the wave of refugees leaving East Germany for the West had increased and had found its way through Hungary via Czechoslovakia (or via the West German Embassy in Prague), tolerated by the new Krenz government and in agreement with the communist Czechoslovak government. </span>

	<span id="sent250">To ease the complications, the politburo led by Krenz decided on November 9, to allow refugees to exit directly through crossing points between East Germany and West Germany, including West Berlin. </span>

	<span id="sent251">On the same day, the ministerial administration modified the proposal to include private travel. </span>

	<span id="sent252">The new regulations were to take effect on November 17, 1989.<br /><br /></span>

