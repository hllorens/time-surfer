	<span id="sent271">Crowds on both sides waited there for hours, cheering at the bulldozers which took parts of the Wall away to reinstate old roads. </span>

	<span id="sent272">Photos and television footage of these events is sometimes mislabelled ``dismantling of the Wall'', even though it was merely the construction of new crossings. </span>

	<span id="sent273">New border crossings continued to be opened through the middle of 1990, including the Brandenburg Gate on December 22, 1989.<br /><br /></span>

	<span id="sent274">West Germans and West Berliners were allowed visa-free travel starting December 23. </span>

	<span id="sent275">Until then they could only visit East Germany and East Berlin under restrictive conditions that involved application for a visa several days or weeks in advance, and obligatory exchange of at least 25 DM per day of their planned stay, all of which hindered spontaneous visits. </span>

	<span id="sent276">Thus, in the weeks between November 9 and December 23, East Germans could actually travel more freely than Westerners.<br /><br /></span>

