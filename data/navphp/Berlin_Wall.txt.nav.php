	<span id="sent231">The Fall.<br /><br /></span>

	<span id="sent232">After allowing for loopholes throughout the summer, Hungary effectively disabled its physical border defenses with Austria on August 19, 1989, and in September more than 13,000 East German tourists escaped through Hungary to Austria. </span>

	<span id="sent233">This set up a chain of events. </span>

	<span id="sent234">The Hungarians prevented many more East Germans from crossing the border and returned them to Budapest. </span>

	<span id="sent235">These East Germans flooded the West German embassy and refused to return to East Germany. </span>

	<span id="sent236">The East German government responded by disallowing any further travel to Hungary, but allowed those already there to return. </span>

	<span id="sent237">This triggered a similar incident in neighboring Czechoslovakia. </span>

	<span id="sent238">On this occasion, the East German authorities allowed them to leave, providing that they used a train which transited East Germany on the way. </span>

	<span id="sent239">This was followed by mass demonstrations within East Germany itself. </span>

