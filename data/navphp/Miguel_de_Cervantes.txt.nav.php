	<span id="sent149">Cervantes es sumamente original. </span>

	<span id="sent150">Parodiando un género que empezaba a periclitar, como el de los libros de caballerías, creó otro género sumamente vivaz, la novela polifónica, donde se superponen las cosmovisiones y los puntos de vista hasta confundirse en complejidad con la misma realidad, recurriendo incluso a juegos metaficcionales. </span>

	<span id="sent151">En la época la épica podía escribirse también en prosa, y con el precedente en el teatro del poco respeto a los modelos clásicos de Lope de Vega, le cupo a él en suma fraguar la fórmula del realismo en la narrativa tal y como había sido preanunciada en España por toda una tradición literaria desde el Cantar del Mío Cid, ofreciéndosela a Europa, donde Cervantes tuvo más discípulos que en España. </span>

	<span id="sent152">La novela realista entera del siglo XIX está marcada por este magisterio. </span>

	<span id="sent153">Por otra parte, otra gran obra maestra de Cervantes, las Novelas ejemplares, demuestra la amplitud de miras de su espíritu y su deseo de experimentar con las estructuras narrativas. </span>

	<span id="sent154">En esta colección de novelas el autor experimenta con la novela bizantina ( La española inglesa), la novela policíaca o criminal ( La fuerza de la sangre, El celoso extremeño), el diálogo lucianesco ( El coloquio de los perros), la miscelánea de sentencias y donaires ( El licenciado Vidriera), la novela picaresca ( Rinconete y Cortadillo), la narración constituida sobre una anagnórisis ( La gitanilla), etc.<br /><br />Obra de Cervantes.<br /><br /></span>

	<span id="sent155">Novelas.<br /><br /></span>

	<span id="sent156">Miguel de Cervantes cultivó, pero a su original modo, los géneros narrativos habituales en la segunda mitad del siglo XVI: la novela bizantina, la novela pastoril, la novela picaresca, la novela morisca, la sátira lucianesca, la miscelánea. </span>

	<span id="sent157">Renovó un género, la novella, que se entendía entonces a la italiana como relato breve, exento de retórica y de mayor trascendencia.<br /><br /></span>

	<span id="sent158">Orden cronológico:<br /><br />Escribió La Galatea en 1585.<br /></span>

	<span id="sent159">Escribió El ingenioso hidalgo don Quijote de la Mancha en 1605.<br /></span>

	<span id="sent160">Escribió Novelas ejemplares en 1613.<br /></span>

	<span id="sent161">Escribió El ingenioso caballero don Quijote de la Mancha en 1615.<br /></span>

	<span id="sent162">Escribió Los trabajos de Persiles y Sigismunda en 1617.<br /><br /><br /></span>

	<span id="sent163">La Galatea.<br /><br /></span>

	<span id="sent164">La Galatea fue la primera novela de Cervantes, en 1585. </span>

	<span id="sent165">Forma parte del subgénero pastoril (una «égloga en prosa» como define el autor), triunfante en el Renacimiento. </span>

	<span id="sent166">Su primera publicación apareció cuando tenía 38 años con el título de Primera parte de La Galatea. </span>

	<span id="sent167">Como en otras novelas del género (similar al de La Diana de Jorge de Montemayor), los personajes son pastores idealizados que relatan sus cuitas y expresan sus sentimientos en una naturaleza idílica ( locus amoenus).<br /><br /></span>

	<span id="sent168">La Galatea se divide en seis libros en los cuales se desarrollan una historia principal y cuatro secundarias que comienzan en el amanecer y finalizan al anochecer, como en las églogas tradicionales, pero de la misma manera que en los poemas bucólicos de Virgilio cada pastor es en realidad una máscara que representa a un personaje verdadero.<br /><br /></span>

	<span id="sent169">Don Quijote de la Mancha.<br /><br /></span>

	<span id="sent170">Es la novela cumbre de la literatura en lengua española. </span>

	<span id="sent171">Su primera parte apareció en 1605 y obtuvo una gran acogida pública. </span>

	<span id="sent172">Pronto se tradujo a las principales lenguas europeas y es una de las obras con más traducciones del mundo.<br /><br /></span>

	<span id="sent173">En un principio, la pretensión de Cervantes fue combatir el auge que habían alcanzado los libros de caballerías, satirizándolos con la historia de un hidalgo manchego que perdió la cordura por leerlos, creyéndose caballero andante. </span>

	<span id="sent174">Para Cervantes, el estilo de las novelas de caballerías era pésimo, y las historias que contaba eran disparatadas. </span>

	<span id="sent175">A pesar de ello, a medida que iba avanzando el propósito inicial fue superado, y llegó a construir una obra que reflejaba la sociedad de su tiempo y el comportamiento humano.<br /><br /></span>

	<span id="sent176">Es probable que Cervantes se inspirara en el Entremés de los romances, en el que un labrador pierde el juicio por su afición a los héroes del Romancero viejo.<br /><br /></span>

	<span id="sent177">Novelas ejemplares.<br /><br /></span>

	<span id="sent178">Entre 1590 y 1612 Cervantes escribió una serie de novelas cortas (pues el término novela se usaba en la época en el mismo sentido que su étimo, el italiano novella, esto es, lo que hoy llamamos novela corta o relato largo) que después acabaría reuniendo en 1613 en la colección de las Novelas ejemplares, dada la gran acogida que obtuvo con la primera parte del Don Quijote. </span>

	<span id="sent179">En un principio recibieron el nombre de Novelas ejemplares de honestísimo entretenimiento.<br /><br /></span>

	<span id="sent180">Dado que existen dos versiones de Rinconete y Cortadillo y de El celoso extremeño, se piensa que Cervantes introdujo en estas novelas algunas variaciones con propósitos morales, sociales y estéticos (de ahí el nombre de «ejemplares»). </span>

	<span id="sent181">La versión más primitiva se encuentra en el llamado manuscrito de Porras de la Cámara, una colección miscelánea de diversas obras literarias entre las cuales se encuentra una novela habitualmente atribuida también a Cervantes, La tía fingida. </span>

	<span id="sent182">Por otra parte, algunas novelas cortas se hallan también insertas en el Don Quijote, como El curioso impertinente o una Historia del cautivo que cuenta con elementos autobiográficos. </span>

	<span id="sent183">Además, se alude a otra novela ya compuesta, Rinconete y Cortadillo.<br /><br /></span>

	<span id="sent184">La gitanilla es la más larga de las novelas ejemplares, y puede tener elementos autobiográficos en una historia amorosa que tuvo un pariente lejano de Cervantes. </span>

	<span id="sent185">Como muchas otras de estas tramas, se centra en el artificio de la agnición o reconocimiento de una persona al final de la obra. </span>

	<span id="sent186">Se trata de una muchacha de origen noble raptada por unos gitanos y educada por ellos, y un noble que se enamora y decide llevar vida gitanil tras ella, hasta que al fin se descubre todo y la historia termina felizmente, posibilitándose el matrimonio de la pareja.<br /><br /></span>

	<span id="sent187">El amante liberal es una novela morisca donde también aparece el tema del rapto, con la historia de un joven siciliano llamado Ricardo que es raptado al igual que a la bellísima Leonisa que es vendida a dos moros por un judío para regalársela al gran turco, entramada con líos amorosos, y aventuras.<br /><br /></span>

	<span id="sent188">En Rinconete y Cortadillo dos muchachos «se desgarran» (se fugan de la casa familiar) y emprenden una vida picaresca con ayuda de la baraja y del hurto, hasta que van a parar a Sevilla, donde mientras trabajan de esportilleros son captados por una asociación mafiosa de malhechores, una especie de sindicato del crimen sevillano gobernado como una cofradía por el hermano mayor, Monipodio. </span>

	<span id="sent189">Se suceden diversas escenas de género propias de un entremés o una jácara donde se presentan alguaciles corruptos, ladrones, matones, chulos y prostitutas; acabado este desfile de tipos, los pillos muchachos deciden regenerarse.<br /><br /></span>

	<span id="sent190">En La española inglesa el rapto vuelve a aparecer en la persona de una muchacha arrebatada en la invasión inglesa de Cádiz y que se educa en Londres como dama de compañía de la reina Isabel I de Inglaterra, que aparece descrita sin animadversión. </span>

	<span id="sent191">Pierde el cabello por un bebedizo pero todo se arregla al final.<br /><br /></span>

	<span id="sent192">En El licenciado Vidriera el estudiante pobre Tomás Rodaja marcha a Salamanca a estudiar acompañando a un noble y allí se licencia con honores; viaja por diversas ciudades de Italia, pero pierde la razón a causa de un filtro de amor que le han suministrado en secreto y cree tener el cuerpo de vidrio y ser sumamente frágil. </span>

	<span id="sent193">Sin embargo, su agudeza es sorprendente y todos le consultan. </span>

	<span id="sent194">La novela es en realidad una colección de las agudezas en prosa del protagonista, al estilo de una de las misceláneas tan frecuentes en el Siglo de Oro. </span>

	<span id="sent195">Finalmente recobra el juicio, pero ya nadie le contrata ni va a verle.<br /><br /></span>

	<span id="sent196">En La fuerza de la sangre se construye un relato casi policíaco, en el que una doncella violada con los ojos tapados logra reconstruir intelectualmente el crimen hasta dar con el culpable y forzar de él que se case con ella restituyendo su honor.<br /><br /></span>

	<span id="sent197">El celoso extremeño narra los celos patológicos de un viejo indiano que vuelve a España enriquecido y encierra a su jovencísima esposa en una casa herméticamente, sin permitirle que salga ni que nada masculino pase la puerta, en la cual ha instalado como vigilante a un esclavo negro con orden de no dejar pasar a nadie. </span>

	<span id="sent198">El seductor Loaysa lo logra engatusando al negro, al que le encanta la música, con una vihuela, y se acuesta con la moza. </span>

	<span id="sent199">Sin embargo, no hacen nada, aunque en el manuscrito de Porras de la Cámara sí se consuman los cuernos. </span>

	<span id="sent200">El viejo, humillado, se muere de pena.<br /><br /></span>

	<span id="sent201">En La ilustre fregona dos jóvenes de buena familia, Carriazo y Avendaño, deciden lanzarse a la vida picaresca. </span>

	<span id="sent202">En un mesón de Toledo Avendaño se enamora de Constanza, una fregona o sirvienta, lo que hará que los dos jóvenes decidan detener allí su viaje. </span>

	<span id="sent203">Finalmente se descubrirá que Constanza es de noble nacimiento, hija natural del padre de Carriazo, por lo que nada impedirá su boda con Avendaño.<br /><br /></span>

	<span id="sent204">El casamiento engañoso narra el timo que hace una señorita aparentemente honesta a un militar casándose con él; éste ignora que ha sido una meretriz y esta lo abandona dejándole una enfermedad venérea que debe purgar con sudores en el hospital de Atocha, donde transcurre la próxima novela.<br /><br /></span>

	<span id="sent205">En El coloquio de los perros el militar, que está purgando su enfermedad en medio de fuertes fiebres, asiste de noche a la conversación entre dos perros, Cipión y Berganza; uno cuenta al otro la historia de su vida y sus muchos (y muy sinvergüenzas) amos y dejan para el día siguiente la relación del otro. </span>

	<span id="sent206">Se trata de una fantasía al estilo de las de Luciano de Samosata y el desfile entremesil de tipos, entre ellos unos pastores y una bruja, recuerda al de una novela picaresca o un entremés.<br /><br /></span>

	<span id="sent207">Los trabajos de Persiles y Sigismunda.<br /><br /></span>

	<span id="sent208">Es la última obra de Cervantes. </span>

	<span id="sent209">Pertenece al subgénero de la novela bizantina. </span>

	<span id="sent210">En ella escribió la dedicatoria a Pedro Fernández de Castro y Andrade, VII Conde de Lemos, el 19 de abril de 1616, cuatro días antes de fallecer, donde se despide de la vida citando estos versos:<br /><br />El autor ve claramente que le queda poca vida y se despide de sus amigos; no se hace ilusiones, pero sin embargo desea vivir y terminar obras que tiene en el magín, cuyo título escribe: Las semanas del jardín, El famoso Bernardo y una segunda parte de La Galatea. </span>

	<span id="sent211">En el género de la novela bizantina, cuenta Cervantes, se atreve a competir con el modelo del género, Heliodoro.<br /><br /></span>

	<span id="sent212">La novela, inspirada en la crónica de Saxo Gramático y Olao Magno y en las fantasías del Jardín de flores curiosas de Francisco de Torquemada, cuenta la peregrinación llevada a cabo por Persiles y Sigismunda, dos príncipes nórdicos enamorados que se hacen pasar por hermanos cambiándose los nombres por Periandro y Auristela. </span>

	<span id="sent213">Separados por todo tipo de peripecias, emprenden un viaje desde el norte de Europa hasta Roma, pasando por España, con finalidad expiatoria antes de contraer matrimonio. </span>

	<span id="sent214">La obra es importante porque supone en el autor un cierto distanciamiento de las fórmulas realistas que hasta el momento ha cultivado, pues aparecen hechos tan peregrinos como que una mujer salte de un campanario librándose de estrellarse gracias al paracaídas que forman sus faldas o que haya personajes que adivinen el futuro. </span>

	<span id="sent215">Los personajes principales aparecen algo desvaídos y en realidad la obra está protagonizada por un grupo, en el que se integran dos españoles abandonados en una isla desierta, Antonio y su hijo, criado en la isla como una especie de bárbaro arquero en contacto con la naturaleza. </span>

	<span id="sent216">Los últimos pasajes del libro están poco limados, ya que el autor falleció antes de corregirlos. </span>

	<span id="sent217">La obra tuvo cierto éxito y se reimprimió varias veces, pero fue olvidada en el siglo siguiente.<br /><br /></span>

	<span id="sent218">Poesía.<br /><br /></span>

	<span id="sent219">Cervantes se afanó en ser poeta, aunque llegó a dudar de su capacidad, como él mismo dijo antes de su muerte en Viaje del Parnaso:<br /><br />Se han perdido o no se han identificado casi todos los versos que no estaban incluidos en sus novelas o en sus obras teatrales; aunque se le suele llamar inventor de los versos de cabo roto, en realidad no fue él. </span>

	<span id="sent220">Cervantes declara haber compuesto gran número de romances, entre los cuales estimaba especialmente uno sobre los celos. </span>

	<span id="sent221">En efecto, hacia 1580 participó con otros grandes poetas contemporáneos como Lope de Vega, Góngora o Quevedo en la imitación de los romances antiguos que dio origen al Romancero nuevo, llamado así frente al tradicional Romancero viejo del siglo XV, el cual era anónimo.<br /><br /></span>

	<span id="sent222">Inicia su obra poética con las cuatro composiciones dedicadas a Exequias de la reina Isabel de Valois. </span>

	<span id="sent223">Otros poemas fueron: A Pedro Padilla, A la muerte de Fernando de Herrera, A la Austriada de Juan Rufo. </span>

	<span id="sent224">Como poeta sin embargo destaca en el tono cómico y satírico, y sus obras maestras son los sonetos Un valentón de espátula y greguesco y Al túmulo del rey Felipe II, del cual se hizo famoso los últimos versos:<br /><br />La Epístola a Mateo Vázquez es una falsificación escrita por el erudito decimonónico Adolfo de Castro, como asimismo lo es el folleto en prosa El buscapié, una vindicación del Don Quijote escrita también por este erudito. </span>

	<span id="sent225">Asentó algunas innovaciones en la métrica, como la invención de la estrofa denominada ovillejo y el uso del soneto con estrambote.<br /><br /></span>

	<span id="sent226">Viaje del Parnaso.<br /><br /></span>

	<span id="sent227">El único poema narrativo extenso de Cervantes es El viaje del Parnaso ( 1614). </span>

	<span id="sent228">Está formado por tercetos encadenados, donde el autor critica a algunos poetas españoles, satirizando a algunos y elogiando a otros. </span>

	<span id="sent229">Escrito, como dice el propio Miguel, a imagen y semejanza del Viaggio di Parnaso ( 1578) de Cesare Caporali di Perugia. </span>

	<span id="sent230">Narra de forma autobiográfica en ocho capítulos un viaje al monte Parnaso, a bordo de una galera dirigida por Mercurio, en la que algunos poetas elogiados tratan de defenderlo frente a los poetastros o malos poetas. </span>

	<span id="sent231">Reunidos en el monte con Apolo, salen airosos de la batalla y el protagonista regresa de nuevo a su hogar. </span>

	<span id="sent232">La obra se completa con la Adjunta al Parnaso, donde Pancracio de Roncesvalles entrega a Cervantes dos epístolas de Apolo.<br /><br /></span>

	<span id="sent233">Teatro.<br /><br /></span>

	<span id="sent234">Dadas sus penurias económicas, el teatro fue la gran vocación de Cervantes. </span>

	<span id="sent235">Escribe que cuando era mozo «se le iban los ojos» tras el carro de los comediantes y que asistió a las austeras representaciones de Lope de Rueda. </span>

	<span id="sent236">Sin embargo, su éxito, que lo tuvo, pues sus obras se representaron «sin ofrenda de pepinos», como dice en el prólogo a sus Ocho comedias y ocho entremeses nunca representados, fue efímero ante el exitazo de la nueva fórmula dramática de Lope de Vega, más audaz y moderna que la suya, que hizo a los empresarios desestimar las comedias cervantinas y preferir las de su rival. </span>

	<span id="sent237">El teatro de Cervantes poseía un fin moral, incluía personajes alegóricos y procuraba someterse a las tres unidades aristotélicas de acción, tiempo y lugar, mientras que el de Lope rompía con esas unidades y era moralmente más desvergonzado y desenvuelto, así como mejor y más variadamente versificado. </span>

	<span id="sent238">Cervantes nunca pudo sobrellevar este fracaso y se mostró disgustado con el nuevo teatro lopesco en la primera parte del Don Quijote, cuyo carácter teatral aparece bien asentado a causa de la abundancia de diálogos y de situaciones de tipo entremesil que entreverán la trama. </span>

	<span id="sent239">Y es, en efecto, el entremés el género dramático donde luce en todo su esplendor el genio dramático de Cervantes, de forma que puede decirse que junto a Luis Quiñones de Benavente y Francisco de Quevedo es Cervantes uno de los mejores autores del género, al que aportó una mayor profundidad en los personajes, un humor inimitable y un mayor calado y trascendencia en la temática. </span>

	<span id="sent240">Que existía interconexión entre el mundo teatral y el narrativo de Cervantes lo demuestra que, por ejemplo, el tema del entremés de El viejo celoso aparezca en la novela ejemplar de El celoso extremeño. </span>

	<span id="sent241">Otras veces aparecen personajes sanchopancescos, como en el entremés de la Elección de los alcaldes de Daganzo, donde el protagonista es tan buen catador o «mojón» de vinos como Sancho. </span>

	<span id="sent242">El barroco tema de la apariencia y la realidad se muestra en El retablo de las maravillas, donde se adapta el cuento medieval de Don Juan Manuel (que Cervantes conocía y había leído en una edición contemporánea) del rey desnudo y se le da un contenido social. </span>

	<span id="sent243">El juez de los divorcios tocaba también biográficamente a Cervantes, y en él se llega a la conclusión de que «más vale el peor concierto / que no el divorcio mejor». </span>

	<span id="sent244">También poseen interés los entremeses de El rufián viudo, La cueva de Salamanca, El vizcaíno fingido y La guarda cuidadosa. </span>

	<span id="sent245">Para sus entremeses adopta Cervantes tanto la prosa como el verso y se le atribuyen algunos otros, como el de Los habladores.<br /><br /></span>

	<span id="sent246">En sus piezas mayores el teatro de Cervantes ha sido injustamente poco apreciado y representado, a excepción de la que representa el ejemplo más acabado de imitación de las tragedias clásicas: El cerco de Numancia, también titulada La destrucción de Numancia, donde se escenifica el tema patriótico del sacrificio colectivo ante el asedio del general Escipión y donde el hambre toma la forma de sufrimiento existencial, añadiéndose figuras alegóricas que profetizan un futuro glorioso para España. </span>

	<span id="sent247">Se trata de una obra donde la Providencia parece tener el mismo cometido que tenía para el Eneas escapado de la Troya incendiada en Virgilio. </span>

	<span id="sent248">Parecida inspiración patriótica poseen otras comedias, como La conquista de Jerusalén, descubierta recientemente. </span>

	<span id="sent249">Otras comedias suyas tratan el tema, que tan directamente padeció el autor y al que incluso se hace alusión en un pasaje de su última obra, el Persiles, del cautiverio en Argel, como Los baños de Argel, Los tratos de Argel, La gran sultana y El gallardo español, donde se ha querido también encontrar la denuncia de la situación de los antiguos soldados como el propio Cervantes. </span>

	<span id="sent250">De tema más novelesco son La casa de los celos y selvas de Ardenia, El laberinto de amor, La entretenida. </span>

	<span id="sent251">Carácter picaresco tienen Pedro de Urdemalas y El rufián dichoso.<br /><br /></span>

	<span id="sent252">Cervantes reunió sus obras no representadas en Ocho comedias y ocho entremeses nunca representados; además, se conservan otras obras en manuscrito: Los tratos de Argel, El gallardo español, La gran sultana y Los baños de Argel.<br /><br /></span>

	<span id="sent253">Obras perdidas y atribuidas.<br /><br /></span>

	<span id="sent254">Cervantes mencionó en algunas oportunidades otras obras que estaba escribiendo o pensaba escribir, así como varias comedias suyas que habían sido representadas con éxito y cuyos textos se han perdido.<br /><br /></span>

	<span id="sent255">Entre las obras que se ignora si llegó a escribir o a completar cabe mencionar la segunda parte de La Galatea, El famoso Bernardo (quizá un libro de caballerías referido a Bernardo del Carpio) y Las semanas del jardín. </span>

	<span id="sent256">También es posible que haya pensado en escribir una continuación del libro de caballerías Belianís de Grecia.<br /><br /></span>

	<span id="sent257">Las obras de teatro perdidas, que Cervantes enumera son La gran Turquesca, La batalla naval, La Jerusalén, La Amaranta o la del Mayo, El bosque amoroso, La única, La bizarra Arsinda y La Confusa, que figuraba en el repertorio del autor Juan Acacio aún en 1627. </span>

	<span id="sent258">También fue obra suya una comedia llamada El trato de Constantinopla y muerte de Selim.<br /><br /></span>

	<span id="sent259">Hay diversas obras que se han atribuido a Cervantes, con variado fundamento. </span>

	<span id="sent260">Entre las más conocidas cabe mencionar:<br /><br />Cervantes en el cine.<br /><br /></span>

