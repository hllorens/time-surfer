	<span id="sent135">Los romances.<br /><br /></span>

	<span id="sent136">Lope pertenece y encabeza, con su eterno rival el cordobés Luis de Góngora, una precoz generación poética que se da a conocer en la década que va de 1580 a 1590. </span>

	<span id="sent137">Desde su juventud estos poetas empiezan a ser conocidos y celebrados. </span>

	<span id="sent138">Los autores  -es obvio - no tuvieron mayor interés en controlar ni exigir nada a los impresores. </span>

	<span id="sent139">Son, en general, poetas jóvenes (Lope, Góngora, Pedro Liñán de Riaza), con menos de treinta años. </span>

	<span id="sent140">Nadie se preocupó de reclamar su autoría, al menos directamente. </span>

	<span id="sent141">La crítica moderna se ha ocupado de dilucidar la autoría de tal o cual romance, pero no ha hecho el esfuerzo necesario para intentar con seriedad establecer el corpus romanceril de los distintos poetas. </span>

	<span id="sent142">El de Lope se ha quedado en vagas aproximaciones. </span>

	<span id="sent143">Mucho se ha hablado sobre el sentido y el alcance de este romancero de la generación de 1580, en el que Lope impone unas pautas recreadas por otros muchos. </span>

	<span id="sent144">El protagonismo de nuestro poeta fue reconocido desde el primer momento. </span>

	<span id="sent145">El nuevo romancero fue una fórmula literaria que caló rápidamente en la sensibilidad social. </span>

	<span id="sent146">Jóvenes que estaban llamados a ser genios creadores de larga trayectoria propusieron a sus lectores y oyentes un feliz híbrido de convencional fantasía y unas referencias en clave a amores y amoríos, favores y desdenes, gustos y disgutos de la actividad erótica. </span>

	<span id="sent147">Pero el exhibicionismo sentimental no se presenta en ellos desnudo. </span>

	<span id="sent148">Aparece, para mayor encanto, velado por la fantasía heroica de los romances moriscos o por la melancolía pastoril. </span>

	<span id="sent149">La añeja tradición de los romances fronterizos, compuestos en su mayor parte en el siglo XV al hilo de los hechos históricos a que aluden, reverdece a finales del siglo XVI en este género de moda. </span>

	<span id="sent150">Los moriscos fueron los primeros romances de moda compuestos por la generación de 1580 [vid. «Ensíllenme el asno rucio»; «Mira, Zaide, que te aviso»]. </span>

	<span id="sent151">La moda del romancero morisco fue sustituida por la pastoril, aunque hubo un tiempo de convivencia de ambas [ vid. «De pechos sobre una torre»; «Hortelano era Belardo»]». </span>

	<span id="sent152">Cf. sobre este último aspecto Francisco de Quevedo: Historia de la vida del buscón. </span>

	<span id="sent153">Edición de Ignacio Arellano. </span>

	<span id="sent154">Madrid: Espasa, 2002, p. 129: «Item, advirtiendo que después que dejaron de ser moros (aunque todavía conservan algunas reliquias) [los poetas] se han metido a pastores, por lo cual andan los ganados flacos de beber sus lágrimas, chamuscados con sus ánimas encendidas, y tan embebecidos en su música que no pacen, mandamos que dejen el tal oficio, señalando ermitas a los amigos de soledad».<br /><br /></span>

	<span id="sent155">Rimas.<br /><br /></span>

	<span id="sent156">En noviembre de 1602, emparedada entre La hermosura de Angélica y La Dragontea, aparecía en la madrileña imprenta de Pedro Madrigal una colección de sonetos: el primer poemario, sin argamasa narrativa, que Lope publicaba a su nombre. </span>

	<span id="sent157">El público debió de acoger favorablemente la colección de doscientos sonetos porque Lope se decidió a publicarlos, sin los poemas épicos, y acompañados de una «Segunda parte», compuesta por églogas, epístolas, epitafios. </span>

	<span id="sent158">Esta nueva edición vio la luz en Sevilla en 1604. </span>

	<span id="sent159">La edición de 1604 enmendaba en ciertos detalles los sonetos publicados en 1602 y reordenaba con buen tino algunos de ellos. </span>

	<span id="sent160">Todavía no había acabado el proceso de acrecentamiento. </span>

	<span id="sent161">En 1609 Lope vuelve a editarlas en Madrid, con la adición del Arte nuevo de hacer comedias. </span>

	<span id="sent162">El impreso, aunque descuidado en grado sumo, tuvo buena acogida. </span>

	<span id="sent163">El texto, que podemos considerar definitivo, con los doscientos sonetos, la Segunda parte y el Arte nuevo, se reimprimió en Milán, 1611; Barcelona, 1612; Madrid, 1613 y 1621; y Huesca, 1623. </span>

	<span id="sent164">En el caso de las «Rimas» encontramos poemas que cabe datar entre 1578 y 1604. </span>

	<span id="sent165">Los doscientos sonetos recorren, desordenadamente y con incrustaciones de otros asuntos, el itinerario obligado de los canzonieri petrarquistas. </span>

	<span id="sent166">Los conflictos amorosos con [Elena] Osorio dieron origen a una celebrada serie sonetil de Lope: la de los mansos. </span>

	<span id="sent167">El motivo pastoril se recrea en una trilogía formada por el soneto «Vireno, aquel mi manso regalado», conservado en el «Cartapacio Penagos» pero no impreso hasta que lo editó Entrambasaguas en 1934, y los sonetos 188 y 189 de las Rimas. </span>

	<span id="sent168">El soneto 126, «Desmayarse, atreverse, estar furioso», se limita a anotar contrarias reacciones, sicológicamente verosímiles, del amante. </span>

	<span id="sent169">Los trece primeros versos han acumulado el predicado de la definición, sin nombrar el sujeto. </span>

	<span id="sent170">El segundo hemistiquio nos quiere convencer, nos convence de que no hemos oído una abstracta e impersonal definición escolástica, sino la expresión artística de una experiencia viva: «quien lo probó lo sabe». </span>

	<span id="sent171">De entre todos los poemas que glosan estos asuntos, alcanzó pronta y perdurable fama el 61: «Ir y quedarse, y con quedar partirse». </span>

	<span id="sent172">Los poemas añadidos en 1604, a pesar de su notable interés, apenas han despertado la curiosidad de críticos y lectores. </span>

	<span id="sent173">Se inicia con tres églogas de distinta factura, interés y calado. </span>

	<span id="sent174">El Arte nuevo de hacer comedias en este tiempo, escrito a finales de 1608, es un poema didáctico, una charla o conferencia y, como tal, se escapa de los estrictos límites de la lírica o la épica.<br /><br /></span>

	<span id="sent175">Rimas sacras.<br /><br /></span>

	<span id="sent176">La primera edición es madrileña, de 1614, con el preciso título de Rimas sacras. </span>

	<span id="sent177">Primera parte. </span>

	<span id="sent178">Que sepamos, nunca hubo una segunda parte. </span>

	<span id="sent179">Estamos ante uno de esos poemarios en los que el autor sinatetiza toda una vena de su fértil musa. </span>

	<span id="sent180">Su estructura corresponde a lo que venimos llamando cancionero lopesco. </span>

	<span id="sent181">Lo integran un canzoniere petrarquista (los cien sonetos iniciales) y una variedad de composiciones en diversos metros y géneros: poesía narrativa en octavas, glosas, romances descriptivos, poemas en tercetos encadenados, liras y canciones. </span>

	<span id="sent182">Las Rimas sacras van a desarrollar ampliamente la palinodia que exigía la tradición literaria del petrarquismo. </span>

	<span id="sent183">No solo porque el soneto inicial sea una reescritura del de Garcilaso de la Vega («Cuando me paro a contemplar mi estado»), sino porque la idea esencial de ofrecer un ejemplo de arrepentimiento del amor mundano está aquí desarrollada, no en un soneto, sino en toda la serie inicial y en otros muchos poemas que pespuntean el «canzoniere» petrarquesco. </span>

	<span id="sent184">La mayoría de los sonetos de las Rimas sacras están escritos en primera persona y dirigidos a un tú íntimo e inmediato. </span>

	<span id="sent185">El más celebrado de todos, el XVIII, es un monólogo del alma, que habla con voces coloquiales y directas a un Jesús enamorado: «¿Qué tengo yo, que mi amistad procuras?». </span>

	<span id="sent186">Frente a estos sonetos de la intimidad, se encuentran, en número menor pero relevante, los de carácter hagiográfico, litúrgico o conmemorativo. </span>

	<span id="sent187">Sin embargo, algunos poemas narrativos, como «Las lágrimas de la Madalena», el de mayor extensión, son continuación del universo poético predominante en los sonetos. «Las lágrimas» pertenece a una especie de la épica.<br /><br /></span>

	<span id="sent188">La Filomena.<br /><br /></span>

	<span id="sent189">En julio de 1621 apareció en Madrid La Filomena con otras diversas rimas, prosas y versos. </span>

	<span id="sent190">En ese mismo año conoció una nueva edición barcelonesa, obra del más apasionado lopista entre los impresores catalanes: Sebastián de Cormellas. </span>

	<span id="sent191">Volumen, pues, misceláneo, en el que Lope ensaya, con esa permanente vocación experimental que venimos señalando, dos géneros que han irrumpido con fuerza en el panorama literario de su época: la novela y la fábula mitológica; y trata de dar la réplica a sus máximos creadores y perpetuos rivales: Góngora y Cervantes. </span>

	<span id="sent192">El poema que da título al volumen se presenta en dos partes distintas en el metro (octavas frente a silvas), el género (narrativa frente a fábula simbólica de polémica literaria) y la intención. </span>

	<span id="sent193">La primera parte, en tres cantos, narra la trágica historia de Filomena, violada y mutilada por su cuñado Tereo, según el conocido relato ovidiano del libro VI de las Metamorfosis. «Las fortunas de Diana», novela corta, no se halla exenta tampoco de afán polémico y espíritu de emulación. </span>

	<span id="sent194">Estamos ante un coletazo, casi último, de la agria disputa que surgió a raíz de la publicación del Quijote. </span>

	<span id="sent195">Primera parte (1605) y la respuesta del círculo de Lope en el apócrifo, firmado por Alonso Fernández de Avellaneda (1614). </span>

	<span id="sent196">Pero no es nuestro objeto comentar el arte narrativo de las «Novelas a Marcia Leonarda», sino señalar su dimensión lírica. </span>

	<span id="sent197">Su núcleo principal es una nueva entrega de romances pastoriles. «La Andrómeda» es un poema emparentado con «La Filomena» aunque algo más breve: 704 versos en un único canto. </span>

	<span id="sent198">Narra con su habitual soltura, y con menos digresiones de las habituales, la historia de Perseo, la muerte de la Medusa, el nacimiento de Pegaso, el surgimiento de la fuente de Hipocrena. </span>

	<span id="sent199">Mucho más interesantes son las epístolas poéticas que vienen a continuación, entre las que se incluyen dos que no son de Lope.<br /><br /></span>

	<span id="sent200">La Circe.<br /><br /></span>

	<span id="sent201">La Circe con otros poemas y prosas aparece en Madrid en 1624. </span>

	<span id="sent202">La Circe es un volumen misceláneo, gemelo de La Filomena, aunque con matices y diferencias. </span>

	<span id="sent203">El poema que da título al volumen es una réplica y, en cierto modo, una superación del modelo de la fábula mitológica fijado por Góngora. </span>

	<span id="sent204">En dos sentidos: en su extensión y complejidad y en su alcance moral. </span>

	<span id="sent205">Un narrador omnisciente presenta al lector la trágica caída de Troya. </span>

	<span id="sent206">El mismo narrador nos cuenta cómo los soldados de Ulises abren los odres de Eolo ha encerrado los vientos y, en medio de la tempestad llegan a la isla de Circe. </span>

	<span id="sent207">Asistimos a la transformación de los soldados en animales. </span>

	<span id="sent208">Vencida Circe, los amigos de Ulises recuperan su imagen originaria. </span>

	<span id="sent209">Parte Ulises, pero aún ha de descender a los infiernos para consultar su porvenir con el adivino Tiresias. «La rosa blanca» es el segundo poema mitológico de este volumen, más breve y concentrado que La Circe, con 872 versos en octavas. </span>

	<span id="sent210">Reúne en rápida sucesión una serie de episodios míticos vinculados a la diosa Venus. </span>

	<span id="sent211">Como en La Filomena, Lope reservó las tres novelas «A la señora Marcia Leonarda» para insertar la aportación de versos castellanos que tenemos en todos sus poemarios. </span>

	<span id="sent212">No abusa de ellos: tres o cuatro poemas originales acoge cada una de las narraciones. </span>

	<span id="sent213">Las seis epístolas en verso de La Circe (hay tres más en prosa) son prolongación y depuración del género y del talante poético que vimos en La Filomena.<br /><br /></span>

	<span id="sent214">Triunfos divinos.<br /><br /></span>

	<span id="sent215">A los diez años de sacerdocio, en medio de las polémicas literarias en torno al culteranismo, Lope volvió a la poesía sagrada como un instrumento más para acercarse al poder político y al eclesiástico. </span>

	<span id="sent216">Estas circunstancias son evidentes en Triunfos divinos (Madrid, 1625), dedicado a la condesa de Olivares. </span>

	<span id="sent217">El poema extenso que da título al volumen es una versión a lo divino de los Triomphi del Petrarca. </span>

	<span id="sent218">La parte más viva del poemario son los sonetos que continúan la línea penitencial e introspectiva del volumen de 1614. </span>

	<span id="sent219">Con portadilla propia, dirigido a la reina Isabel de Borbón, cierra el volumen un poema épico breve (tres cantos; 904 versos) titulado La Virgen de la Almudena.<br /><br /></span>

	<span id="sent220">Laurel de Apolo.<br /><br /></span>

	<span id="sent221">Dentro de la campaña con la que Lope trata de proyectar su figura entre las altas esferas y en los círculos literarios debe incluirse la publicación del Laurel de Apolo (1630). </span>

	<span id="sent222">El poema central, que da título al volumen, es el acta de unas cortes del Parnaso. </span>

	<span id="sent223">Para esta transcripción emplea como estrofa la silva. </span>

	<span id="sent224">Se propuso Lope elogiar a los poetas de su tiempo y así lo hizo. </span>

	<span id="sent225">A lo largo de diez silvas, desfilan cerca de trescientos vates españoles y portugueses, treinta y seis italianos y franceses y diez pintores ilustres. </span>

	<span id="sent226">Dentro del largo catálogo de poetas se insertan algunas fábulas mitológicas, dos de ellas con su propio título identificativo (El baño de Diana, El Narciso). </span>

	<span id="sent227">Aprovecha además Lope para atacar indirectamente a su rival por el puesto de Cronista del Reino de Castilla y León, José Pellicer de Salas y Tovar, quien era, además, uno de los comentaristas de su gran enemigo, Luis de Góngora, cuyo estilo se critica también en el Laurel de Apolo a través de sus malos seguidores. </span>

	<span id="sent228">El volumen del Laurel de Apolo, aunque ocupado en su mayor parte por el extenso poema que he descrito, tiene un apéndice que no carece de interés. </span>

	<span id="sent229">Allí encontramos La selva sin amor, égloga pastoril, una silva, una epístola y un manojuelo de ocho sonetos, entre los que siempre se han destacado las sátiras anticulteranas: " Boscán, tarde llegamos. ¿Hay posada...?".<br /><br /></span>

	<span id="sent230">La vega del Parnaso.<br /><br /></span>

	<span id="sent231">Entre los poemarios de Lope, este presenta una historia muy peculiar. </span>

	<span id="sent232">Su núcleo está integrado por una serie de composiciones líricas de cierta extensión impresas como pliegos sueltos o folletos de escasas páginas en los últimos años de la vida del poeta. </span>

	<span id="sent233">Lope pensó en dar a la imprenta El Parnaso, pero no llevó a efecto su propósito. </span>

	<span id="sent234">El nuevo poemario no vio la luz hasta que, muerto el autor, sus amigos y herederos lo publicaron en 1637 en la Imprenta del Reino con el título de La vega del Parnaso. </span>

	<span id="sent235">En La vega se reunieron obras de muy distinto calado, intención e importancia. </span>

	<span id="sent236">Se incluyeron los impresos sueltos anteriores a 1633 que ya han sido mencionados. </span>

	<span id="sent237">Se recuperaron textos antiguos. </span>

	<span id="sent238">Se acumularon poemas de ocasión de la última etapa de Lope. </span>

	<span id="sent239">Se agruparon también algunas obras escritas en los últimos meses de vida del poeta. </span>

	<span id="sent240">Esta mezcla de dramas y poemas líricos es enteramente ajena a los hábitos editoriales de Lope. </span>

	<span id="sent241">La vega del Parnaso constituye la penúltima revolución lírica de Lope. </span>

	<span id="sent242">En varios poemas emplea dos tipos de lira de seis versos. </span>

	<span id="sent243">Con este metro busca una expresión más escueta. </span>

	<span id="sent244">Es un abandono momentáneo de su larga trayectoria de poeta petrarquista y amoroso para intentar una poesía volcada hacia lo social que le granjeara el respeto y el auxilio de la corte. </span>

	<span id="sent245">Uno de los temas clave del poemario es la conciencia de la muerte.<br /><br /></span>

	<span id="sent246">Rimas humanas y divinas del licenciado Burguillos.<br /><br /></span>

	<span id="sent247">En noviembre de 1634 acaba de imprimirse en la Imprenta del Reino, a costa de Alonso Pérez, el último poemario que Lope verá en vida: Rimas humanas y divinas del licenciado Tomé de Burguillos. </span>

	<span id="sent248">El libro tiene la estructura habitual de los cancioneros lopescos: un cancionero petrarquista (formado por la mayor parte de los 161 sonetos), que resulta fundamentalmente paródico y humorístico, ya que se centra en una lavandera del Manzanares, Juana, a la que pretende el autor, una máscara o heterónimo de Lope, el estudiante pobre Tomé de Burguillos. </span>

	<span id="sent249">Junto a estos poemas hay otros epigramáticos, humorísticos, serios, desengañados, satíricos, jocosos, religiosos e incluso filosóficos, que pertenecen al tranquilo ciclo de senectute lopesco, así como una excepcional epopeya cómico-burlesca, La Gatomaquia, en siete silvas, sin duda la más perfecta y acabada muestra del género épico que salió de la pluma de Lope, protagonizada por gatos. </span>

	<span id="sent250">En la portada aparece eñ «licenciado Tomé de Burguillos», y un retrato grabado del mismo; su sintética biografía se nos da en el «Advertimiento al señor lector». </span>

	<span id="sent251">Burguillos, paralelo en cierto sentido a la figura del donaire en la comedia, encarna la visión antiheroica, escéptica y desengañada del viejo Lope, que parodia en él su propia biografía y su creación literaria. </span>

	<span id="sent252">Sin embargo la aprobación del amigo del autor, Francisco de Quevedo, deja entrever que su estilo es bien parecido "al que floreció sin espinas en Lope de Vega". </span>

	<span id="sent253">Burguillos traza un «canzoniere» petrarquista en clave de parodia, de autoparodia.<br /><br /></span>

	<span id="sent254">La lírica en el teatro.<br /><br /></span>

	<span id="sent255">Con Lope de Vega, hacia 1585, el teatro español recupera su primitiva vocación lírica. </span>

	<span id="sent256">Al fin, los creadores de la comedia nueva son los mismos que han puesto de moda los romances moriscos y pastoriles. </span>

	<span id="sent257">Numerosas comedias de nuestro autor tienen su origen en canciones de carácter tradicional.<br /><br /></span>

	<span id="sent258">Obra épica.<br /><br /></span>

	<span id="sent259">La dragontea.<br /><br /></span>

	<span id="sent260">Lope dedicó una parte considerable de los esfuerzos de sus mejores años a convertirse en el poeta épico español. </span>

	<span id="sent261">El primero publicado, La dragontea, tuvo en su aparición notables problemas. </span>

	<span id="sent262">El permiso para publicarlo le fue denegado por las autoridades castellanas en 1598, razón por la que el libro hubo de imprimirse en Valencia. </span>

	<span id="sent263">Amparándose en este permiso valenciano, Lope pidió de nuevo la autorización para publicarlo en Castilla. </span>

	<span id="sent264">No sólo no se permitió la nueva edición, sino que se mandó recoger los ejemplares que circulaban en el reino de Castilla. </span>

	<span id="sent265">No cejó el poeta y, disimulado tras «La hermosura de Angélica» y los doscientos sonetos, lo publicó en Madrid en 1602. </span>

	<span id="sent266">En las 732 octavas (5.856 versos) narra las correrías de sir Francis Drake.<br /><br /></span>

	<span id="sent267">Isidro.<br /><br /></span>

	<span id="sent268">Lo más vivo del poema hagiográfico Isidro (Madrid, 1599), sobre la vida del patrono de Madrid, San Isidro Labrador, escrito en quintillas a lo largo de diez cantos son, sin disputa, los fragmentos abundantes en que el poeta se acerca al universo rural en que se mueve el santo; en efecto, Lope amó de todo corazón la vida sencilla de los campesinos y ansió toda su vida el contacto directo con la naturaleza. </span>

	<span id="sent269">Pero este poema biográfico es algo más que eso, ya que se halla sólidamente documentado: leyó todo lo escrito anteriormente sobre el santo y tuvo acceso a los papeles de la causa de beatificación recogidos por el padre Domingo de Mendoza, comisario pontificio para la beatificación de Isidro.<br /><br /></span>

	<span id="sent270">La hermosura de Angélica.<br /><br /></span>

	<span id="sent271">Este poema se publicó en 1602, junto a las Rimas y La dragontea; va dedicado a su amigo sevillano, el poeta Juan de Arguijo; en el prólogo dice Lope que lo escribió en los momentos que le dejaba libre la vida marinera, "sobre las aguas, entre las jarcias del galeón San Juan y las banderas del Rey Católico", continuando los flecos de la historia de Angélica que Ludovico Ariosto trazó en su Orlando furioso, pues él mismo propuso a otros ingenios que continuasen si lo hacían mejor. </span>

	<span id="sent272">Traspasa la historia de Angélica a España y traza con sus aventuras y desventuras veinte cantos en octavas reales.<br /><br /></span>

	<span id="sent273">Jerusalén conquistada.<br /><br /></span>

	<span id="sent274">En 1604, en el prólogo a la edición sevillana de las «Rimas», Lope anunciaba la inminente aparición de un nuevo poema épico. </span>

	<span id="sent275">La obra no vio la luz hasta febrero de 1609. </span>

	<span id="sent276">El texto que se imprimió no tiene dieciséis libros sino veinte. </span>

	<span id="sent277">Lapesa sugiere que el texto original se remataba con la coronación de Ricardo Corazón de León como rey de Jerusalén; pero, para igualar el número de cantos del poema tassiano (Jerusalén liberada, 1581), Lope añadió cuatro en los que tuvo que narrar el abandono de la empresa por los cruzados.<br /><br /></span>

	<span id="sent278">Obra dramática.<br /><br /></span>

	<span id="sent279">La creación de la Comedia Nueva.<br /><br /></span>

	<span id="sent280">Lope de Vega creó el teatro clásico español del Siglo de Oro con una novedosa fórmula dramática. </span>

	<span id="sent281">En dicha fórmula mezclaba lo trágico y lo cómico, y rompía las tres unidades que propugnaba la escuela de poética italiana (Ludovico Castelvetro, Francesco Robortello) fundada en la Poética y la Retórica de Aristóteles: unidad de acción (que se cuente una sola historia), unidad de tiempo (en 24 horas o un poco más) y de lugar (que transcurra en un solo lugar o en sitios aledaños).<br /><br /></span>

	<span id="sent282">En cuanto a la unidad de acción, las comedias de Lope utilizan el imbroglio o embrollo italiano (contar dos historias o más en la misma obra, por lo general una principal y otra secundaria, o una protagonizada por nobles y otra por sus sirvientes plebeyos). </span>

	<span id="sent283">La de tiempo, es recomendable pero no siempre se acata, y hay comedias que narran la vida entera de un individuo, si bien recomendaba hacer coincidir el paso del tiempo con los entreactos. </span>

	<span id="sent284">Con respecto a la de lugar, no se acata en absoluto.<br /><br /></span>

	<span id="sent285">Es más, Lope de Vega no respeta una cuarta unidad, la unidad de estilo o decoro que se encuentra también esbozada en Aristóteles, y mezcla en su obra lo trágico y lo cómico y se vale de un teatro polimétrico que utiliza distintos tipos de verso y estrofa, según el fondo de lo que se está representando. </span>

	<span id="sent286">Utiliza el romance cuando un personaje hace relaciones, esto es, cuenta hechos; la octava real cuando se trata de hacer relaciones lucidas o descripciones; redondillas y quintillas cuando se trata de diálogos; sonetos cuando se trata de monólogos introspectivos o esperas o cuando los personajes deben de cambiar de traje entre bambalinas; décimas si se trata de quejas o lamentos. </span>

	<span id="sent287">El verso predominante es el octosílabo, algo menos el endecasílabo, seguidos de todos los demás. </span>

	<span id="sent288">Se trata, pues, de un teatro polimétrico y poco académico, a diferencia del teatro clásico francés, y en ese sentido se parece más al teatro isabelino.<br /><br /></span>

	<span id="sent289">Por otra parte, domina el tema sobre la acción y la acción sobre la caracterización. </span>

	<span id="sent290">Los tres temas principales de su teatro son el amor, la fe y el honor, y se encuentra entreverado de hermosos intermedios líricos, muchos de ellos de origen popular (Romancero, lírica tradicional). </span>

	<span id="sent291">Se escogen preferentemente los temas relacionados con la honra ("mueven con fuerza a toda gente", escribe Lope) y se rehúye la sátira demasiado descubierta.<br /><br /></span>

	<span id="sent292">Lope se cuidaba especialmente del público femenino, que podía hacer fracasar una función, y recomendaba "engañar con la verdad" y hacer creer al público en desenlaces que luego no ocurrían al menos hasta mitad de la tercera jornada; recomendaba algunos trucos, como travestir a las actrices con disfraz varonil, cosa que excitaba la imaginación libidinosa del público masculino y que en el futuro se extendería en el teatro cómico universal como un ardid de guión habitual en la comedia de todos los tiempos: la guerra de sexos, esto es, trastocar los roles masculino y femenino. </span>

	<span id="sent293">Mujeres impetuosas que se comportan como hombres y hombres indecisos que se comportan como mujeres. </span>

	<span id="sent294">Todos estos preceptos recomienda Lope a quienes quieren seguir su fórmula dramática en su Arte nuevo de hacer comedias en este tiempo (1609), escrito en verso blanco salteado de pareados para una academia literaria.<br /><br /></span>

	<span id="sent295">Clasificación y principales obras dramáticas.<br /><br /></span>

	<span id="sent296">Las obras dramáticas de Lope fueron compuestas sólo para la escena y el autor no se reservaba ninguna copia. </span>

	<span id="sent297">El ejemplar sufría los cortes, adecuaciones, ampliaciones y retoques de los actores, alguno de ellos escritores de comedias también.<br /><br /></span>

	<span id="sent298">Entre 1604 y 1647 se publican veinticinco tomos de Partes que recopilan las comedias de Lope, aunque los primeros salieron a la luz sin el consentimiento del dramaturgo. </span>

	<span id="sent299">Éste sólo tomó las riendas de la edición de su propia obra a partir de la Parte IX (1617) y hasta su muerte, cuando tenía en imprenta las partes XXI y XXII. </span>

	<span id="sent300">Juan Pérez de Montalbán, escritor de comedias discípulo suyo, afirma en su Fama póstuma que escribió unas mil ochocientas comedias y cuatrocientos autos sacramentales, de las que se ha perdido una gran parte. </span>

	<span id="sent301">El propio autor fue más modesto y en sus obras estimó que había escrito unas mil quinientas, lo que puede entenderse incluyendo incluso los autos sacramentales y otras obras escénicas; pero aun así resulta una cifra muy crecida. </span>

	<span id="sent302">Para explicarlo Charles Vincent Aubrun ha supuesto que el dramaturgo sólo trazaba el plan y componía algunas escenas sueltas, mientras que otros poetas y actores de su taller completaban la obra; sin embargo los poetas de la época no tenían empacho en declarar su autoría en obras en colaboración de hasta tres ingenios, así que no puede sostenerse ese punto de vista, por más que la fama del autor hiciese prudente ocultar sus ayudas para vender mejor la obra. </span>

	<span id="sent303">Rennert y Castro hicieron un serio estudio que concluye que la megalómana cifra anda exagerada y que se le pueden atribuir con firmeza 723 títulos, de los cuales 78 son de atribución dudosa o errada y 219 se han perdido, así que el repertorio dramático de Lope se reduciría a 426 piezas. </span>

	<span id="sent304">No obstante Morley y Bruerton, valiéndose, aunque no exclusivamente, de criterios métricos que después se han comprobado muy seguros, estrecharon aún más los criterios y establecieron indudablemente como suyas 316 comedias, 73 como dudosas y 87 que, comúnmente atribuidas a Lope, no son suyas.<br /><br /></span>

	<span id="sent305">La lista de comedias notables es ciertamente muy crecida. </span>

	<span id="sent306">Pueden citarse La discreta enamorada, El acero de Madrid, Los embustes de Celauro, El bobo del colegio, El amor enamorado, Las bizarrías de Belisa, La esclava de su galán, La niña de plata, El arenal de Sevilla, Lo cierto por lo dudoso, La hermosa fea, Los milagros del desprecio, El anzuelo de Fenisa, El rufián Castrucho, El halcón de Federico, La doncella Teodor, La difunta pleiteada, La desdichada Estefanía o El rey don Pedro en Madrid.<br /><br /></span>

	<span id="sent307">De todas éstas se reconocen como obras maestras, si bien en toda obra de Lope siempre hay alguna escena que delata su genialidad, un par de docenas, entre las que se encuentran Peribáñez y el comendador de Ocaña (1610), Fuenteovejuna (1612-1614), La dama boba (1613), Amar sin saber a quién (1620-1622), El mejor alcalde, el rey (1620-1623), El caballero de Olmedo (1620-1625), El castigo sin venganza (1631), El perro del hortelano, El villano en su rincón, El duque de Viseo o Lo fingido verdadero.<br /><br /></span>

	<span id="sent308">El Caballero de Olmedo es una de sus obras más líricas, a la vez que trágica. </span>

	<span id="sent309">Está dividida en tres episodios, que se corresponden con el planteamiento el nudo y el desenlace. </span>

	<span id="sent310">Don Alonso (caballero de Olmedo) le pide ayuda a su criado Tello para conquistar a Inés, de ese modo Tello entra en contacto con Fabia (dotada de rasgos celestinescos) que ayuda a estos dos para que se "correspondan" y se lleguen a casar. </span>

	<span id="sent311">Sin embargo Rodrigo y su hermano Fernando tratarán de impedirlo. </span>

	<span id="sent312">Por fatal desenlace, Alonso muere cerca de un arroyo cuando se dirigía a Olmedo, aún siendo avisado por sueños, etc. en manos de Rodrigo, pero Tello pide justicia al Rey, que los condena a muerte.<br /><br /></span>

	<span id="sent313">Marcelino Menéndez Pelayo, uno de los primeros editores de su teatro, usando por criterio la temática de estas obras y sus fuentes, en once bloques, aunque su clasificación se presenta algo confusa porque a veces se puede clasificar en dos o más categorías una misma comedia:<br /><br />Lope de Vega se documentaba para las comedias de historia nacional en diversas crónicas históricas, en especial en frey Francisco de Rades de Andrada y su Crónica de las tres órdenes y caballerías de Santiago, Calatrava y Alcántara, de la que salió, por ejemplo, el argumento de Fuenteovejuna, o la Crónica de Enrique IV de Alfonso de Palencia.<br /><br /></span>

	<span id="sent314">Aubrun reduce la categoría temática de la comedia lopesca a tres temas: el amor, el honor y la fe. </span>

	<span id="sent315">Francisco Ruiz Ramón, sin embargo, prefiere hablar de dramas del poder injusto entre un noble y un plebeyo o un plebeyo y el rey, o el rey y el noble; de dramas de honor y de dramas de amor. </span>

	<span id="sent316">Otra clasificación distingue entre obras cómicas y dramas, con distintos subgrupos en cada uno:<br /><br />La escuela dramática de Lope de Vega.<br /><br /></span>

	<span id="sent317">Muchos dramaturgos se apuntaron a las novedades de Lope de Vega e incluso perfeccionaron su modelo. </span>

	<span id="sent318">Entre los mayores seguidores estuvieron Guillén de Castro, Antonio Mira de Amescua, Luis Vélez de Guevara y su hijo, Juan Vélez de Guevara, Juan Ruiz de Alarcón y Tirso de Molina. </span>

	<span id="sent319">Figuras menores fueron Miguel Sánchez, Damián Salucio del Poyo, Andrés de Claramonte, Felipe Godínez, Diego Jiménez del Enciso, Rodrigo de Herrera, Alonso Jerónimo de Salas Barbadillo, Jerónimo de Villaizán, Juan Pérez de Montalbán, Luis Belmonte Bermúdez, Antonio Hurtado de Mendoza, Alonso de Castillo Solórzano, Alonso Remón y Jacinto de Herrera, por no mencionar una innumerable serie de ingenios de tercer orden.<br /><br /></span>

