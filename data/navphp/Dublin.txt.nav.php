	<span id="sent260">Live music is popularly played on streets and at venues throughout Dublin in general, and the city has produced several musicians and groups of international success, including U2, Westlife, The Dubliners, The Thrills, Horslips, The Boomtown Rats, Boyzone, Ronan Keating, Thin Lizzy, Paddy Casey, Sinead O'Connor, The Script and My Bloody Valentine. </span>

