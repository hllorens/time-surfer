	<span id="sent219">Culture.<br /><br /></span>

	<span id="sent220">The arts.<br /><br /></span>

	<span id="sent221">Dublin has a world famous literary history, having produced many prominent literary figures, including Nobel laureates William Butler Yeats, George Bernard Shaw and Samuel Beckett. </span>

	<span id="sent222">Other influential writers and playwrights include Oscar Wilde, Jonathan Swift and the creator of Dracula, Bram Stoker. </span>

	<span id="sent223">It is arguably most famous as the location of the greatest works of James Joyce, including Ulysses, which is set in Dublin and full of topical detail. </span>

	<span id="sent224">Dubliners is a collection of short stories by Joyce about incidents and typical characters of the city during the early 20th century. </span>

	<span id="sent225">Other renowned writers include J. M. Synge, Sean O'Casey, Brendan Behan, Maeve Binchy, and Roddy Doyle. </span>

	<span id="sent226">Ireland's biggest libraries and literary museums are found in Dublin, including the National Print Museum of Ireland and National Library of Ireland. </span>

	<span id="sent227">In July 2010, Dublin was named as a UNESCO City of Literature, joining Edinburgh, Melbourne and Iowa City with the permanent title.<br /><br /></span>

	<span id="sent228">There are several theatres within the city centre, and various world famous actors have emerged from the Dublin theatrical scene, including Noel Purcell, Sir Michael Gambon, Brendan Gleeson, Stephen Rea, Colin Farrell, Colm Meaney and Gabriel Byrne. </span>

	<span id="sent229">The best known theatres include the Gaiety, Abbey, Olympia, Gate, and Grand Canal. </span>

	<span id="sent230">The Gaiety specialises in musical and operatic productions, and is popular for opening its doors after the evening theatre production to host a variety of live music, dancing, and films. </span>

	<span id="sent231">The Abbey was founded in 1904 by a group that included Yeats with the aim of promoting indigenous literary talent. </span>

	<span id="sent232">It went on to provide a breakthrough for some of the city's most famous writers, such as Synge, Yeats himself and George Bernard Shaw. </span>

	<span id="sent233">The Gate was founded in 1928 to promote European and American Avant Garde works. </span>

	<span id="sent234">The Grand Canal Theatre is a new 2,111 capacity theatre which opened in March 2010 in the Grand Canal Dock.<br /><br /></span>

	<span id="sent235">Apart from being the focus of the country's literature and theatre, Dublin is also the focal point for much of Irish Art and the Irish artistic scene. </span>

	<span id="sent236">The Book of Kells, a world-famous manuscript produced by Celtic Monks in AD 800 and an example of Insular art, is on display in Trinity College. </span>

	<span id="sent237">The Chester Beatty Library houses the famous collection of manuscripts, miniature paintings, prints, drawings, rare books and decorative arts assembled by American mining millionaire (and honorary Irish citizen) Sir Alfred Chester Beatty (1875 -1968). </span>

	<span id="sent238">The collections date from 2700 BC onwards and are drawn from Asia, the Middle East, North Africa and Europe. </span>

	<span id="sent239">Work by local artists is often put on public display around St. Stephen's Green, the main public park in the city centre. </span>

	<span id="sent240">In addition large art galleries are found across the city, including the Irish Museum of Modern Art, the National Gallery, the Hugh Lane Municipal Gallery, The City Arts Centre, The Douglas Hyde Gallery, The Project Arts Centre and The Royal Hibernian Academy. </span>

	<span id="sent241">Three branches of the National Museum of Ireland are located in Dublin: Archaeology in Kildare Street, Decorative Arts and History in Collins Barracks and Natural History in Merrion Street.<br /><br /></span>

	<span id="sent242">Dublin has long been a city with a strong underground arts scene. </span>

	<span id="sent243">Temple Bar was the home of many artists in the 1980s, and spaces such as the Project Arts Centre were hubs for collectives and new exhibitions. </span>

	<span id="sent244">The Guardian noted that Dublin's independent and underground arts flourished during the economic recession of 2010. </span>

	<span id="sent245">Dublin also has many acclaimed dramatic, musical and operatic companies, including Festival Productions, Lyric Opera Productions, The Pioneers Musical & Dramatic Society, The Glasnevin Musical Society, Second Age Theatre Company, Opera Theatre Company, and Opera Ireland. </span>

	<span id="sent246">Ireland is well known for its love of baroque music, which is highly acclaimed at Trinity College. </span>

	<span id="sent247">Perhaps the most famous Dublin theatre company is the renowned Rathmines and Rathgar Musical Society, which has been in existence since 1913. </span>

	<span id="sent248">It produced full scale productions of popular musicals and operettas including Oklahoma!, Carousel, The Mikado, Guys and Dolls, The Pirates of Penzance, Me and My Girl, My Fair Lady, The Yeoman of the Guard, Gigi, Fiddler on the Roof, The Gondoliers, Anything Goes, The Merry Widow, Iolanthe, The Producers and HMS Pinafore. </span>

	<span id="sent249">At present, the society is performing a tribute concert to the works of Rodgers and Hammerstein at the National Concert Hall. </span>

	<span id="sent250">The society will recreate their 1913 production of The Mikado this November 2010 at the National Concert Hall.<br /><br /></span>

	<span id="sent251">Entertainment.<br /><br /></span>

	<span id="sent252">Dublin has a vibrant nightlife and is reputedly one of Europe's most youthful cities, with an estimate of 50% of citizens being younger than 25. </span>

	<span id="sent253">There are many pubs across the city centre, with the area around St. Stephen's Green and Grafton Street, especially Harcourt Street, Camden Street, Wexford Street and Leeson Street, having the most popular nightclubs and pubs.<br /><br /></span>

	<span id="sent254">The best known area for nightlife is Temple Bar, south of the River Liffey. </span>

	<span id="sent255">To some extent, the area has become popular among tourists, including stag and hen parties from Britain. </span>

	<span id="sent256">It was developed as Dublin's cultural quarter and does retain this spirit as a centre for small arts productions, photographic and artists' studios, and in the form of street performers and small music venues. </span>

