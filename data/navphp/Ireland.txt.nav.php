	<span id="sent321">Economy.<br /><br /></span>

	<span id="sent322">Ireland was largely passed over by the industrial revolution. </span>

	<span id="sent323">One reason given why Ireland did not experience an industrial revolution is because of the scarcity of coal and iron, resources that facilitate an industrial revolution. </span>

	<span id="sent324">However, there were other countries that lacked these resources, but nonetheless industrialised, so there may be other reasons why Ireland did not industrialise. </span>

	<span id="sent325">Nineteenth century explanations for why Ireland did not industrialise did not blame the absence of natural resources but that, ``The fault is not in the country, but in ourselves; the absence of successful enterprise is owing to the fact, that we do not know how to succeed ... we want special industrial knowledge.'' </span>

	<span id="sent326">Some historians today point to the sudden union with the structurally superior economy of England. </span>

	<span id="sent327">They point out that iron and coal prices in Ireland were as cheap as they were in parts of England outside of the mining centres  - and a little cheaper in some parts  - and that, on the eve of union with Great Britain, Ireland was industrialising (particularly the linen industry). </span>

