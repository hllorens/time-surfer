	<span id="sent204">In the Republic of Ireland, counties form the basis of the system of local government. </span>

	<span id="sent205">Counties Dublin, Cork, Limerick, Galway, Waterford and Tipperary have been broken up into smaller administrative areas. </span>

	<span id="sent206">However, they are still treated as counties for cultural and some official purposes, for example post and by the Ordnance Survey Ireland. </span>

	<span id="sent207">Counties in Northern Ireland are no longer used for local governmental purposes, but, as in the Republic, their traditional boundaries are still used for informal purposes such as sports leagues and in cultural or tourism contexts.<br /><br /></span>

