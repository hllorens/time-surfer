	<span id="sent199">Traditionally, Ireland is subdivided into four provinces: Connacht (west), Leinster (east), Munster (south), and Ulster (north). </span>

	<span id="sent200">In a system that developed between the 13th and 17th centuries, Ireland has thirty-two traditional counties. </span>

	<span id="sent201">Twenty-six of the counties are in the Republic of Ireland and six counties are in Northern Ireland. </span>

	<span id="sent202">The six counties that constitute Northern Ireland are all in the province of Ulster (which has nine counties in total). </span>

