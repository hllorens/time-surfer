	<span id="sent213">All-island institutions.<br /><br /></span>

	<span id="sent214">Despite the political partition, the island of Ireland continues to act as a single entity in a number of areas that transcend governmental agencies. </span>

	<span id="sent215">The two jurisdictions share transport, telecommunications, energy and water systems. </span>

	<span id="sent216">With a few notable exceptions, the island is the main organisational unit for major religious, cultural and sporting organisations. </span>

	<span id="sent217">The island fields a single international team in most sports, for example, and March 17 is celebrated throughout Ireland as the traditional Irish holiday of St. Patrick's Day. </span>

	<span id="sent218">One notable exception to this is Association football, although both associations continued to field international teams under the name ``Ireland'' until the 1950s. </span>

	<span id="sent219">An all-Ireland club competition for soccer, the Setanta Cup, was created in 2005.<br /><br /></span>

	<span id="sent220">The 1998 Belfast Agreement provides for political co-operation between the two jurisdictions. </span>

	<span id="sent221">The North-South Ministerial Council, established under the agreement, is an institution through which ministers from the Government of Ireland and the Northern Ireland Executive can formulate all-island policies in twelve ``areas of co-operation'' such as agriculture, the environment and transport. </span>

	<span id="sent222">Six of these policy areas have associated all-island ``implementation bodies''. </span>

	<span id="sent223">For example, food safety is managed by the Food Safety Promotion Board and Tourism Ireland markets the island as a whole. </span>

	<span id="sent224">Three major political parties, Sinn Fein, the Irish Green Party and, most recently, Fianna Fail, are organised on an all-island basis. </span>

	<span id="sent225">However, only the former two of these have contested elections and hold legislative seats in both jurisdictions.<br /><br /></span>

	<span id="sent226">Despite the two jurisdictions using two distinct currencies (the euro and pound sterling), a growing amount of commercial activity is carried out on an all-island basis. </span>

	<span id="sent227">This has in part been facilitated by the two jurisdictions' shared membership of the European Union. </span>

