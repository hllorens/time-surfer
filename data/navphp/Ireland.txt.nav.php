	<span id="sent231">Geography.<br /><br /></span>

	<span id="sent232">The island of Ireland is located in the north-west of Europe, between latitudes 51 and 56 N, and longitudes 11 and 5 W. It is separated from the neighbouring island of Great Britain by the Irish Sea and the North Channel, which has a width of 23kilometres (14mi) at its narrowest point. </span>

	<span id="sent233">To the west is the northern Atlantic Ocean and to the south is the Celtic Sea, which spans south-eastwardly from Ireland to Brittany, in France. </span>

	<span id="sent234">Ireland and Great Britain, together with nearby islands, are known collectively as the British Isles. </span>

	<span id="sent235">As the term British Isles is controversial in relation to Ireland, the alternate term Ireland and Britain is often used as a neutral term for the islands.<br /><br /></span>

	<span id="sent236">A ring of coastal mountains surround low plains at the centre of the island. </span>

	<span id="sent237">The highest of these is Carrauntoohil ( Irish: Corran Tuathail) in County Kerry, which rises to 1,038m (3,406ft) above sea level. </span>

	<span id="sent238">The most arable land lies in the province of Leinster. </span>

	<span id="sent239">Western areas can be mountainous and rocky with green panoramic vistas. </span>

	<span id="sent240">The River Shannon, the island's longest river at 386km (240mi) long, rises in County Cavan in the north west and flows 113kilometres (70mi) to Limerick city in the mid west.<br /><br /></span>

	<span id="sent241">The island's lush vegetation, a product of its mild climate and frequent rainfall, earns it the sobriquet the Emerald Isle. </span>

	<span id="sent242">Overall, Ireland has a mild but changeable oceanic climate with few extremes. </span>

	<span id="sent243">The climate is typically insular and is temperate avoiding the extremes in temperature of many other areas in the world at similar latitudes. </span>

	<span id="sent244">This is a result of the moderating moist winds which ordinarily prevail from the South-Western Atlantic.<br /><br /></span>

	<span id="sent245">Precipitation falls throughout the year but is light overall, particularly in the east. </span>

	<span id="sent246">The west tends to be wetter on average and prone to Atlantic storms, especially in the late autumn and winter months. </span>

	<span id="sent247">These occasionally bring destructive winds and higher total rainfall to these areas, as well as sometimes snow and hail. </span>

	<span id="sent248">The regions of north County Galway and east County Mayo have the highest incidents of recorded lightning annually for the island, with lightening occurring approximately five to ten days per year in these areas. </span>

	<span id="sent249">Munster, in the south, records the least snow whereas Ulster, in the north, records the most.<br /><br /></span>

	<span id="sent250">Inland areas are warmer in summer and colder in winter. </span>

	<span id="sent251">Usually around 40 days of the year are below freezing 0 C (32 F) at inland weather stations, compared to 10 days at coastal stations. </span>

	<span id="sent252">Ireland is sometimes affected by heat waves, most recently in 1995, 2003 and 2006. </span>

	<span id="sent253">In common with the rest of Europe, Ireland experienced unusually cold weather during the winter of 2009/10. </span>

	<span id="sent254">Temperatures fell as low as 13 C (9 F) in some parts and up to a metre (3ft) of snow in mountainous areas.<br /><br /></span>

	<span id="sent255">The island consists of varied geological provinces. </span>

	<span id="sent256">In the far west, around County Galway and County Donegal, is a medium to high grade metamorphic and igneous complex of Caledonide affinity, similar to the Scottish Highlands. </span>

	<span id="sent257">Across southeast Ulster and extending southwest to Longford and south to Navan is a province of Ordovician and Silurian rocks, with similarities to the Southern Uplands province of Scotland. </span>

	<span id="sent258">Further south, along the County Wexford coastline, is an area of granite intrusives into more Ordovician and Silurian rocks, like that found in Wales. </span>

	<span id="sent259">In the southwest, around Bantry Bay and the mountains of Macgillicuddy's Reeks, is an area of substantially deformed, but only lightly metamorphosed, Devonian-aged rocks. </span>

	<span id="sent260">This partial ring of ``hard rock'' geology is covered by a blanket of Carboniferous limestone over the centre of the country, giving rise to a comparatively fertile and lush landscape. </span>

	<span id="sent261">The west-coast district of the Burren around Lisdoonvarna has well developed karst features. </span>

