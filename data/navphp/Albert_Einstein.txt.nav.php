	<span id="sent207">The theory of Brownian motion was the least revolutionary of Einstein's Annus mirabilis papers, but it is the most frequently cited, and had an important role in securing the acceptance of the atomic theory by physicists.<br /><br /></span>

	<span id="sent208">Thought experiments and a-priori physical principles.<br /><br /></span>

	<span id="sent209">Einstein's thinking underwent a transformation in 1905. </span>

	<span id="sent210">He had come to understand that quantum properties of light mean that Maxwell's equations were only an approximation. </span>

	<span id="sent211">He knew that new laws would have to replace these, but he did not know how to go about finding those laws. </span>

	<span id="sent212">He felt that guessing formal relations would not go anywhere.<br /><br /></span>

	<span id="sent213">So he decided to focus on a-priori principles instead, which are statements about physical laws which can be understood to hold in a very broad sense even in domains where they have not yet been shown to apply. </span>

	<span id="sent214">A well accepted example of an a-priori principle is rotational invariance. </span>

	<span id="sent215">If a new force is discovered in physics, it is assumed to be rotationally invariant almost automatically, without thought. </span>

	<span id="sent216">Einstein sought new principles of this sort, to guide the production of physical ideas. </span>

	<span id="sent217">Once enough principles are found, then the new physics will be the simplest theory consistent with the principles and with previously known laws.<br /><br /></span>

	<span id="sent218">The first general a-priori principle he found was the principle of relativity, that uniform motion is indistinguishable from rest. </span>

	<span id="sent219">This was understood by Hermann Minkowski to be a generalization of rotational invariance from space to space-time. </span>

	<span id="sent220">Other principles postulated by Einstein and later vindicated are the principle of equivalence and the principle of adiabatic invariance of the quantum number. </span>

	<span id="sent221">Another of Einstein's general principles, Mach's principle, is fiercely debated, and whether it holds in our world or not is still not definitively established.<br /><br /></span>

	<span id="sent222">The use of a-priori principles is a distinctive unique signature of Einstein's early work, and has become a standard tool in modern theoretical physics.<br /><br /></span>

	<span id="sent223">Special relativity.<br /><br /></span>

	<span id="sent224">His 1905 paper on the electrodynamics of moving bodies introduced his theory of special relativity, which showed that the observed independence of the speed of light on the observer's state of motion required fundamental changes to the notion of simultaneity. </span>

	<span id="sent225">Consequences of this include the time-space frame of a moving body slowing down and contracting (in the direction of motion) relative to the frame of the observer. </span>

	<span id="sent226">This paper also argued that the idea of a luminiferous aether - one of the leading theoretical entities in physics at the time - was superfluous. </span>

	<span id="sent227">In his paper on mass -energy equivalence, which had previously been considered to be distinct concepts, Einstein deduced from his equations of special relativity what has been called the twentieth century's best-known equation: E= mc . </span>

	<span id="sent228">This equation suggests that tiny amounts of mass could be converted into huge amounts of energy and presaged the development of nuclear power. </span>

	<span id="sent229">Einstein's 1905 work on relativity remained controversial for many years, but was accepted by leading physicists, starting with Max Planck.<br /><br /></span>

	<span id="sent230">Photons.<br /><br /></span>

	<span id="sent231">In a 1905 paper, Einstein postulated that light itself consists of localized particles ( quanta). </span>

	<span id="sent232">Einstein's light quanta were nearly universally rejected by all physicists, including Max Planck and Niels Bohr. </span>

	<span id="sent233">This idea only became universally accepted in 1919, with Robert Millikan's detailed experiments on the photoelectric effect, and with the measurement of Compton scattering.<br /><br /></span>

	<span id="sent234">Einstein's paper on the light particles was almost entirely motivated by thermodynamic considerations. </span>

	<span id="sent235">He was not at all motivated by the detailed experiments on the photoelectric effect, which did not confirm his theory until fifteen years later. </span>

	<span id="sent236">Einstein considers the entropy of light at temperature T, and decomposes it into a low-frequency part and a high-frequency part. </span>

	<span id="sent237">The high-frequency part, where the light is described by Wien's law, has an entropy which looks exactly the same as the entropy of a gas of classical particles.<br /><br /></span>

	<span id="sent238">Since the entropy is the logarithm of the number of possible states, Einstein concludes that the number of states of short wavelength light waves in a box with volume V is equal to the number of states of a group of localizable particles in the same box. </span>

	<span id="sent239">Since (unlike others) he was comfortable with the statistical interpretation, he confidently postulates that the light itself is made up of localized particles, as this is the only reasonable interpretation of the entropy.<br /><br /></span>

	<span id="sent240">This leads him to conclude that each wave of frequency f is associated with a collection of photons with energy hf each, where h is Planck's constant. </span>

	<span id="sent241">He does not say much more, because he is not sure how the particles are related to the wave. </span>

	<span id="sent242">But he does suggest that this idea would explain certain experimental results, notably the photoelectric effect.<br /><br /></span>

	<span id="sent243">Quantized atomic vibrations.<br /><br /></span>

	<span id="sent244">Einstein continued his work on quantum mechanics in 1906, by explaining the specific heat anomaly in solids. </span>

	<span id="sent245">This was the first application of quantum theory to a mechanical system. </span>

	<span id="sent246">Since Planck's distribution for light oscillators had no problem with infinite specific heats, the same idea could be applied to solids to fix the specific heat problem there. </span>

	<span id="sent247">Einstein showed in a simple model that the hypothesis that solid motion is quantized explains why the specific heat of a solid goes to zero at zero temperature.<br /><br /></span>

	<span id="sent248">Einstein's model treats each atom as connected to a single spring. </span>

	<span id="sent249">Instead of connecting all the atoms to each other, which leads to standing waves with all sorts of different frequencies, Einstein imagined that each atom was attached to a fixed point in space by a spring. </span>

	<span id="sent250">This is not physically correct, but it still predicts that the specific heat is 3 Nk B, since the number of independent oscillations stays the same.<br /><br /></span>

	<span id="sent251">Einstein then assumes that the motion in this model is quantized, according to the Planck law, so that each independent spring motion has energy which is an integer multiple of hf, where f is the frequency of oscillation. </span>

	<span id="sent252">With this assumption, he applied Boltzmann's statistical method to calculate the average energy of the spring. </span>

	<span id="sent253">The result was the same as the one that Planck had derived for light: for temperatures where k B T is much smaller than hf, the motion is frozen, and the specific heat goes to zero.<br /><br /></span>

	<span id="sent254">So Einstein concluded that quantum mechanics would solve the main problem of classical physics, the specific heat anomaly. </span>

	<span id="sent255">The particles of sound implied by this formulation are now called phonons. </span>

	<span id="sent256">Because all of Einstein's springs have the same stiffness, they all freeze out at the same temperature, and this leads to a prediction that the specific heat should go to zero exponentially fast when the temperature is low. </span>

	<span id="sent257">The solution to this problem is to solve for the independent normal modes individually, and to quantize those. </span>

	<span id="sent258">Then each normal mode has a different frequency, and long wavelength vibration modes freeze out at colder temperatures than short wavelength ones. </span>

	<span id="sent259">This was done by Peter Debye, and after this modification Einstein's quantization method reproduced quantitatively the behavior of the specific heats of solids at low temperatures.<br /><br /></span>

	<span id="sent260">This work was the foundation of condensed matter physics.<br /><br /></span>

	<span id="sent261">Adiabatic principle and action-angle variables.<br /><br /></span>

	<span id="sent262">Throughout the 1910s, quantum mechanics expanded in scope to cover many different systems. </span>

	<span id="sent263">After Ernest Rutherford discovered the nucleus and proposed that electrons orbit like planets, Niels Bohr was able to show that the same quantum mechanical postulates introduced by Planck and developed by Einstein would explain the discrete motion of electrons in atoms, and the periodic table of the elements.<br /><br /></span>

	<span id="sent264">Einstein contributed to these developments by linking them with the 1898 arguments Wilhelm Wien had made. </span>

	<span id="sent265">Wien had shown that the hypothesis of adiabatic invariance of a thermal equilibrium state allows all the blackbody curves at different temperature to be derived from one another by a simple shifting process. </span>

	<span id="sent266">Einstein noted in 1911 that the same adiabatic principle shows that the quantity which is quantized in any mechanical motion must be an adiabatic invariant. </span>

	<span id="sent267">Arnold Sommerfeld identified this adiabatic invariant as the action variable of classical mechanics. </span>

	<span id="sent268">The law that the action variable is quantized was the basic principle of the quantum theory as it was known between 1900 and 1925.<br /><br /></span>

	<span id="sent269">Wave-particle duality.<br /><br /></span>

	<span id="sent270">Although the patent office promoted Einstein to Technical Examiner Second Class in 1906, he had not given up on academia. </span>

	<span id="sent271">In 1908, he became a privatdozent at the University of Bern. </span>

	<span id="sent272">In ``uber die Entwicklung unserer Anschauungen uber das Wesen und die Konstitution der Strahlung'' (`` The Development of Our Views on the Composition and Essence of Radiation''), on the quantization of light, and in an earlier 1909 paper, Einstein showed that Max Planck's energy quanta must have well-defined momenta and act in some respects as independent, point-like particles. </span>

	<span id="sent273">This paper introduced the photon concept (although the name photon was introduced later by Gilbert N. Lewis in 1926) and inspired the notion of wave-particle duality in quantum mechanics.<br /><br /></span>

	<span id="sent274">Theory of critical opalescence.<br /><br /></span>

	<span id="sent275">Einstein returned to the problem of thermodynamic fluctuations, giving a treatment of the density variations in a fluid at its critical point. </span>

	<span id="sent276">Ordinarily the density fluctuations are controlled by the second derivative of the free energy with respect to the density. </span>

	<span id="sent277">At the critical point, this derivative is zero, leading to large fluctuations. </span>

	<span id="sent278">The effect of density fluctuations is that light of all wavelengths is scattered, making the fluid look milky white. </span>

	<span id="sent279">Einstein relates this to Raleigh scattering, which is what happens when the fluctuation size is much smaller than the wavelength, and which explains why the sky is blue.<br /><br /></span>

	<span id="sent280">Zero-point energy.<br /><br /></span>

	<span id="sent281">Einstein's physical intuition led him to note that Planck's oscillator energies had an incorrect zero point. </span>

	<span id="sent282">He modified Planck's hypothesis by stating that the lowest energy state of an oscillator is equal to  2 hf, to half the energy spacing between levels. </span>

	<span id="sent283">This argument, which was made in 1913 in collaboration with Otto Stern, was based on the thermodynamics of a diatomic molecule which can split apart into two free atoms.<br /><br /></span>

	<span id="sent284">Principle of equivalence.<br /><br /></span>

	<span id="sent285">In 1907, while still working at the patent office, Einstein had what he would call his ``happiest thought''. </span>

	<span id="sent286">He realized that the principle of relativity could be extended to gravitational fields. </span>

	<span id="sent287">He thought about the case of a uniformly accelerated box not in a gravitational field, and noted that it would be indistinguishable from a box sitting still in an unchanging gravitational field. </span>

	<span id="sent288">He used special relativity to see that the rate of clocks at the top of a box accelerating upward would be faster than the rate of clocks at the bottom. </span>

	<span id="sent289">He concludes that the rates of clocks depend on their position in a gravitational field, and that the difference in rate is proportional to the gravitational potential to first approximation.<br /><br /></span>

	<span id="sent290">Although this approximation is crude, it allowed him to calculate the deflection of light by gravity, and show that it is nonzero. </span>

	<span id="sent291">This gave him confidence that the scalar theory of gravity proposed by Gunnar Nordstrom was incorrect. </span>

	<span id="sent292">But the actual value for the deflection that he calculated was too small by a factor of two, because the approximation he used doesn't work well for things moving at near the speed of light. </span>

	<span id="sent293">When Einstein finished the full theory of general relativity, he would rectify this error and predict the correct amount of light deflection by the sun.<br /><br /></span>

	<span id="sent294">From Prague, Einstein published a paper about the effects of gravity on light, specifically the gravitational redshift and the gravitational deflection of light. </span>

	<span id="sent295">The paper challenged astronomers to detect the deflection during a solar eclipse. </span>

	<span id="sent296">German astronomer Erwin Finlay-Freundlich publicized Einstein's challenge to scientists around the world.<br /><br /></span>

	<span id="sent297">Einstein thought about the nature of the gravitational field in the years 1909 -1912, studying its properties by means of simple thought experiments. </span>

	<span id="sent298">A notable one is the rotating disk. </span>

	<span id="sent299">Einstein imagined an observer making experiments on a rotating turntable. </span>

	<span id="sent300">He noted that such an observer would find a different value for the mathematical constant pi than the one predicted by Euclidean geometry. </span>

	<span id="sent301">The reason is that the radius of a circle would be measured with an uncontracted ruler, but, according to special relativity, the circumference would seem to be longer because the ruler would be contracted.<br /><br /></span>

	<span id="sent302">Since Einstein believed that the laws of physics were local, described by local fields, he concluded from this that spacetime could be locally curved. </span>

	<span id="sent303">This led him to study Riemannian geometry, and to formulate general relativity in this language.<br /><br /></span>

	<span id="sent304">Hole argument and Entwurf theory.<br /><br /></span>

	<span id="sent305">While developing general relativity, Einstein became confused about the gauge invariance in the theory. </span>

	<span id="sent306">He formulated an argument that led him to conclude that a general relativistic field theory is impossible. </span>

	<span id="sent307">He gave up looking for fully generally covariant tensor equations, and searched for equations that would be invariant under general linear transformations only.<br /><br /></span>

	<span id="sent308">In June, 1913 the Entwurf (``draft'') theory was the result of these investigations. </span>

	<span id="sent309">As its name suggests, it was a sketch of a theory, with the equations of motion supplemented by additional gauge fixing conditions. </span>

	<span id="sent310">Simultaneously less elegant and more difficult than general relativity, after more than two years of intensive work Einstein abandoned the theory in November, 1915 after realizing that the hole argument was mistaken.<br /><br /></span>

	<span id="sent311">General relativity.<br /><br /></span>

	<span id="sent312">In 1912, Einstein returned to Switzerland to accept a professorship at his alma mater, the ETH. </span>

	<span id="sent313">Once back in Zurich, he immediately visited his old ETH classmate Marcel Grossmann, now a professor of mathematics, who introduced him to Riemannian geometry and, more generally, to differential geometry. </span>

	<span id="sent314">On the recommendation of Italian mathematician Tullio Levi-Civita, Einstein began exploring the usefulness of general covariance (essentially the use of tensors) for his gravitational theory. </span>

	<span id="sent315">For a while Einstein thought that there were problems with the approach, but he later returned to it and, by late 1915, had published his general theory of relativity in the form in which it is used today. </span>

	<span id="sent316">This theory explains gravitation as distortion of the structure of spacetime by matter, affecting the inertial motion of other matter. </span>

	<span id="sent317">During World War I, the work of Central Powers scientists was available only to Central Powers academics, for national security reasons. </span>

	<span id="sent318">Some of Einstein's work did reach the United Kingdom and the United States through the efforts of the Austrian Paul Ehrenfest and physicists in the Netherlands, especially 1902 Nobel Prize-winner Hendrik Lorentz and Willem de Sitter of Leiden University. </span>

	<span id="sent319">After the war ended, Einstein maintained his relationship with Leiden University, accepting a contract as an Extraordinary Professor; for ten years, from 1920 to 1930, he travelled to Holland regularly to lecture.<br /><br /></span>

	<span id="sent320">In 1917, several astronomers accepted Einstein 's 1911 challenge from Prague. </span>

	<span id="sent321">The Mount Wilson Observatory in California, U.S., published a solar spectroscopic analysis that showed no gravitational redshift. </span>

	<span id="sent322">In 1918, the Lick Observatory, also in California, announced that it too had disproved Einstein's prediction, although its findings were not published.<br /><br /></span>

	<span id="sent323">However, in May 1919, a team led by the British astronomer Arthur Stanley Eddington claimed to have confirmed Einstein's prediction of gravitational deflection of starlight by the Sun while photographing a solar eclipse with dual expeditions in Sobral, northern Brazil, and Principe, a west African island. </span>

	<span id="sent324">Nobel laureate Max Born praised general relativity as the ``greatest feat of human thinking about nature''; fellow laureate Paul Dirac was quoted saying it was ``probably the greatest scientific discovery ever made''. </span>

	<span id="sent325">The international media guaranteed Einstein's global renown.<br /><br /></span>

	<span id="sent326">There have been claims that scrutiny of the specific photographs taken on the Eddington expedition showed the experimental uncertainty to be comparable to the same magnitude as the effect Eddington claimed to have demonstrated, and that a 1962 British expedition concluded that the method was inherently unreliable. </span>

	<span id="sent327">The deflection of light during a solar eclipse was confirmed by later, more accurate observations. </span>

	<span id="sent328">Some resented the newcomer's fame, notably among some German physicists, who later started the Deutsche Physik (German Physics) movement.<br /><br /></span>

	<span id="sent329">Cosmology.<br /><br /></span>

	<span id="sent330">In 1917, Einstein applied the General theory of relativity to model the structure of the universe as a whole. </span>

	<span id="sent331">He wanted the universe to be eternal and unchanging, but this type of universe is not consistent with relativity. </span>

	<span id="sent332">To fix this, Einstein modified the general theory by introducing a new notion, the cosmological constant. </span>

	<span id="sent333">With a positive cosmological constant, the universe could be an eternal static sphere<br /><br /></span>

	<span id="sent334">Einstein believed a spherical static universe is philosophically preferred, because it would obey Mach's principle. </span>

	<span id="sent335">He had shown that general relativity incorporates Mach's principle to a certain extent in frame dragging by gravitomagnetic fields, but he knew that Mach's idea would not work if space goes on forever. </span>

	<span id="sent336">In a closed universe, he believed that Mach's principle would hold.<br /><br /></span>

	<span id="sent337">Mach's principle has generated much controversy over the years.<br /><br /></span>

	<span id="sent338">Modern quantum theory.<br /><br /></span>

	<span id="sent339">In 1917, at the height of his work on relativity, Einstein published an article in Physikalische Zeitschrift that proposed the possibility of stimulated emission, the physical process that makes possible the maser and the laser. </span>

	<span id="sent340">This article showed that the statistics of absorption and emission of light would only be consistent with Planck's distribution law if the emission of light into a mode with n photons would be enhanced statistically compared to the emission of light into an empty mode. </span>

	<span id="sent341">This paper was enormously influential in the later development of quantum mechanics, because it was the first paper to show that the statistics of atomic transitions had simple laws. </span>

	<span id="sent342">Einstein discovered Louis de Broglie's work, and supported his ideas, which were received skeptically at first. </span>

	<span id="sent343">In another major paper from this era, Einstein gave a wave equation for de Broglie waves, which Einstein suggested was the Hamilton -Jacobi equation of mechanics. </span>

	<span id="sent344">This paper would inspire Schrodinger's work of 1926.<br /><br /></span>

	<span id="sent345">Bose -Einstein statistics.<br /><br /></span>

	<span id="sent346">In 1924, Einstein received a description of a statistical model from Indian physicist Satyendra Nath Bose, based on a counting method that assumed that light could be understood as a gas of indistinguishable particles. </span>

	<span id="sent347">Einstein noted that Bose's statistics applied to some atoms as well as to the proposed light particles, and submitted his translation of Bose's paper to the Zeitschrift fur Physik. </span>

	<span id="sent348">Einstein also published his own articles describing the model and its implications, among them the Bose -Einstein condensate phenomenon that some particulates should appear at very low temperatures. </span>

	<span id="sent349">It was not until 1995 that the first such condensate was produced experimentally by Eric Allin Cornell and Carl Wieman using ultra-cooling equipment built at the NIST - JILA laboratory at the University of Colorado at Boulder. </span>

	<span id="sent350">Bose -Einstein statistics are now used to describe the behaviors of any assembly of bosons. </span>

	<span id="sent351">Einstein's sketches for this project may be seen in the Einstein Archive in the library of the Leiden University.<br /><br /></span>

	<span id="sent352">Energy momentum pseudotensor.<br /><br /></span>

	<span id="sent353">General relativity includes a dynamical spacetime, so it is difficult to see how to identify the conserved energy and momentum. </span>

	<span id="sent354">Noether's theorem allows these quantities to be determined from a Lagrangian with translation invariance, but general covariance makes translation invariance into something of a gauge symmetry. </span>

	<span id="sent355">The energy and momentum derived within general relativity by Noether's presecriptions do not make a real tensor for this reason.<br /><br /></span>

	<span id="sent356">Einstein argued that this is true for fundamental reasons, because the gravitational field could be made to vanish by a choice of coordinates. </span>

	<span id="sent357">He maintained that the non-covariant energy momentum pseudotensor was in fact the best description of the energy momentum distribution in a gravitational field. </span>

	<span id="sent358">This approach has been echoed by Lev Landau and Evgeny Lifshitz, and others, and has become standard.<br /><br /></span>

	<span id="sent359">The use of non-covariant objects like pseudotensors was heavily criticized in 1917 by Erwin Schrodinger and others.<br /><br /></span>

	<span id="sent360">Unified field theory.<br /><br /></span>

	<span id="sent361">Following his research on general relativity, Einstein entered into a series of attempts to generalize his geometric theory of gravitation, which would allow the explanation of electromagnetism. </span>

	<span id="sent362">In 1950, he described his `` unified field theory'' in a Scientific American article entitled ``On the Generalized Theory of Gravitation.'' </span>

	<span id="sent363">Although he continued to be lauded for his work, Einstein became increasingly isolated in his research, and his efforts were ultimately unsuccessful. </span>

	<span id="sent364">In his pursuit of a unification of the fundamental forces, Einstein ignored some mainstream developments in physics, most notably the strong and weak nuclear forces, which were not well understood until many years after his death. </span>

	<span id="sent365">Mainstream physics, in turn, largely ignored Einstein's approaches to unification. </span>

	<span id="sent366">Einstein's dream of unifying other laws of physics with gravity motivates modern quests for a theory of everything and in particular string theory, where geometrical fields emerge in a unified quantum-mechanical setting.<br /><br /></span>

	<span id="sent367">Wormholes.<br /><br /></span>

	<span id="sent368">Einstein collaborated with others to produce a model of a wormhole. </span>

	<span id="sent369">His motivation was to model elementary particles with charge as a solution of gravitational field equations, in line with the program outlined in the paper ``Do Gravitational Fields play an Important Role in the Constitution of the Elementary Particles?''. </span>

	<span id="sent370">These solutions cut and pasted Schwarzschild black holes to make a bridge between two patches.<br /><br /></span>

	<span id="sent371">If one end of a wormhole was positively charged, the other end would be negatively charged. </span>

	<span id="sent372">These properties led Einstein to believe that pairs of particles and antiparticles could be described in this way.<br /><br /></span>

	<span id="sent373">Einstein -Cartan theory.<br /><br /></span>

	<span id="sent374">In order to incorporate spinning point particles into general relativity, the affine connection needed to be generalized to include an antisymmetric part, called the torsion. </span>

	<span id="sent375">This modification was made by Einstein and Cartan in the 1920s.<br /><br /></span>

	<span id="sent376">Einstein -Podolsky -Rosen paradox.<br /><br /></span>

	<span id="sent377">In 1935, Einstein returned to the question of quantum mechanics. </span>

	<span id="sent378">He considered how a measurement on one of two entangled particles would affect the other. </span>

	<span id="sent379">He noted, along with his collaborators, that by performing different measurements on the distant particle, either of position or momentum, different properties of the entangled partner could be discovered without disturbing it in any way.<br /><br /></span>

	<span id="sent380">He then used a hypothesis of local realism to conclude that the other particle had these properties already determined. </span>

	<span id="sent381">The principle he proposed is that if it is possible to determine what the answer to a position or momentum measurement would be, without in any way disturbing the particle, then the particle actually has values of position or momentum.<br /><br /></span>

	<span id="sent382">This principle distilled the essence of Einstein's objection to quantum mechanics. </span>

	<span id="sent383">As a physical principle, it has since been shown to be incompatible with experiments.<br /><br /></span>

	<span id="sent384">Equations of motion.<br /><br /></span>

	<span id="sent385">The theory of general relativity has a fundamental law  - the Einstein equations which describe how space curves, the geodesic equation which describes how particles move may be derived from the Einstein equations.<br /><br /></span>

	<span id="sent386">Since the equations of general relativity are non-linear, a lump of energy made out of pure gravitational fields, like a black hole, would move on a trajectory which is determined by the Einstein equations themselves, not by a new law. </span>

	<span id="sent387">So Einstein proposed that the path of a singular solution, like a black hole, would be determined to be a geodesic from general relativity itself.<br /><br /></span>

	<span id="sent388">This was established by Einstein, Infeld and Hoffmann for pointlike objects without angular momentum, and by Roy Kerr for spinning objects.<br /><br /></span>

	<span id="sent389">Einstein's controversial beliefs in physics.<br /><br /></span>

	<span id="sent390">In addition to his well-accepted results, some of Einstein's views are regarded as controversial:<br /><br /></span>

	<span id="sent391">Collaboration with other scientists.<br /><br /></span>

	<span id="sent392">In addition to long time collaborators Leopold Infeld, Nathan Rosen, Peter Bergmann and others, Einstein also had some one-shot collaborations with various scientists.<br /><br /></span>

	<span id="sent393">Einstein-de Haas experiment.<br /><br /></span>

	<span id="sent394">Einstein and De Haas demonstrated that magnetization is due to the motion of electrons, nowadays known to be the spin. </span>

	<span id="sent395">In order to show this, they reversed the magnetization in an iron bar suspended on a torsion pendulum. </span>

	<span id="sent396">They confirmed that this leads the bar to rotate, because the electron's angular momentum changes as the magnetization changes. </span>

	<span id="sent397">This experiment needed to be sensitive, because the angular momentum associated with electrons is small, but it definitively established that electron motion of some kind is responsible for magnetization.<br /><br /></span>

	<span id="sent398">Schrodinger gas model.<br /><br /></span>

	<span id="sent399">Einstein suggested to Erwin Schrodinger that he might be able to reproduce the statistics of a Bose -Einstein gas by considering a box. </span>

	<span id="sent400">Then to each possible quantum motion of a particle in a box associate an independent harmonic oscillator. </span>

	<span id="sent401">Quantizing these oscillators, each level will have an integer occupation number, which will be the number of particles in it.<br /><br /></span>

	<span id="sent402">This formulation is a form of second quantization, but it predates modern quantum mechanics. </span>

	<span id="sent403">Erwin Schrodinger applied this to derive the thermodynamic properties of a semiclassical ideal gas. </span>

	<span id="sent404">Schrodinger urged Einstein to add his name as co-author, although Einstein declined the invitation.<br /><br /></span>

	<span id="sent405">Einstein refrigerator.<br /><br /></span>

	<span id="sent406">In 1926, Einstein and his former student Leo Szilard co-invented (and in 1930, patented) the Einstein refrigerator. </span>

	<span id="sent407">This Absorption refrigerator was then revolutionary for having no moving parts and using only heat as an input. </span>

	<span id="sent408">On 11 November 1930, U.S. Patent 1,781,541 was awarded to Albert Einstein and Leo Szilard for the refrigerator. </span>

	<span id="sent409">Their invention was not immediately put into commercial production, as the most promising of their patents were quickly bought up by the Swedish company Electrolux to protect its refrigeration technology from competition.<br /><br /></span>

	<span id="sent410">Bohr versus Einstein.<br /><br /></span>

	<span id="sent411">In the 1920s, quantum mechanics developed into a more complete theory. </span>

	<span id="sent412">Einstein was unhappy with the Copenhagen interpretation of quantum theory developed by Niels Bohr and Werner Heisenberg. </span>

	<span id="sent413">In this interpretation, quantum phenomena are inherently probabilistic, with definite states resulting only upon interaction with classical systems. </span>

	<span id="sent414">A public debate between Einstein and Bohr followed, lasting on and off for many years (including during the Solvay Conferences). </span>

	<span id="sent415">Einstein formulated thought experiments against the Copenhagen interpretation, which were all rebutted by Bohr. </span>

