	<span id="sent148">In Boltzmann's way of thinking, the entropy is the logarithm of the number of ways a system could be configured inside. </span>

	<span id="sent149">The reason the entropy goes up is only because it is more likely for a system to go from a special state with only a few possible internal configurations to a more generic state with many. </span>

	<span id="sent150">While Boltzmann's statistical interpretation of entropy is universally accepted today, and Einstein believed it, at the turn of the 20th century it was a minority position.<br /><br /></span>

