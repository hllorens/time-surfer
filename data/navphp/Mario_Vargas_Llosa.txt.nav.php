	<span id="sent1">Mario Vargas Llosa, es un escritor, considerado uno de los más importantes novelistas y ensayistas contemporáneos en lengua española. </span>

	<span id="sent2">Peruano de nacimiento, cuenta también con la nacionalidad española, que obtuvo en 1993. </span>

	<span id="sent3">Su obra ha cosechado numerosos premios, entre los que destacan el Nobel de Literatura en 2010, «por su cartografía de las estructuras del poder y sus imágenes mordaces de la resistencia del individuo, su rebelión y su derrota»; el Premio Cervantes en 1994 y el Premio Príncipe de Asturias de las Letras en 1986, entre otros. </span>

	<span id="sent4">Vargas Llosa alcanzó la fama en la década de 1960 con novelas, tales como La ciudad y los perros (1962), La casa verde (1965) y Conversación en La Catedral (1969). </span>

	<span id="sent5">Continúa escribiendo prolíficamente en una serie de géneros literarios, incluyendo la crítica literaria y el periodismo. </span>

	<span id="sent6">Entre sus novelas se cuentan comedias, novelas policiacas, novelas históricas y políticas. </span>

	<span id="sent7">Varias de ellas, como Pantaleón y las visitadoras (1973) y La fiesta del chivo (2000), han sido adaptadas y llevadas al cine. </span>

	<span id="sent8">Muchas de las obras de Vargas Llosa están influidas por la percepción del escritor sobre la sociedad peruana y por sus propias experiencias como peruano; sin embargo, de forma creciente ha tratado temas de otras partes del mundo. </span>

	<span id="sent9">Ha residido en Europa (entre España, Gran Bretaña, Suiza y Francia) la mayor parte del tiempo desde 1958, cuando inició su carrera literaria, de modo que en su obra se percibe también una fuerte influencia europea. </span>

	<span id="sent10">Al igual que otros autores latinoamericanos, Vargas Llosa ha participado en política a lo largo de su carrera. </span>

	<span id="sent11">Fue candidato a la presidencia del Perú en 1990 por la coalición política de centroderecha Frente Democrático (Fredemo).<br /><br /></span>

