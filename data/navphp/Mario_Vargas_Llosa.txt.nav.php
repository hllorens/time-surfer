	<span id="sent76">Participación política.<br /><br /></span>

	<span id="sent77">En la década de 1980, Vargas Llosa se volvió políticamente activo y causó sorpresa por sus posiciones liberales, ya que la intelectualidad de la época se caracterizaba por su perfil izquierdista.<br /><br /></span>

