	<span id="sent80">En 1987, ante los intentos del gobierno aprista de Alan García de nacionalizar la banca peruana, Vargas Llosa se perfiló como líder político, encabezando la protesta contra esa acción. </span>

	<span id="sent81">Inició su carrera política fundando el movimiento Libertad y se presentó como candidato a la Presidencia del Perú en 1990.<br /><br /></span>

