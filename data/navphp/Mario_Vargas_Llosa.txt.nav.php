	<span id="sent39">En 1960, luego de que culminara su beca en Madrid, Vargas Llosa se mudó a Francia con la impresión de que iba a obtener una beca para estudiar ahí; sin embargo, habiendo llegado a París se enteró que su solicitud había sido denegada. </span>

