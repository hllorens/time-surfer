	<span id="sent14">Fue el único hijo de Ernesto Vargas Maldonado y de Dora Llosa Ureta, quienes se separaron meses antes de su nacimiento. </span>

	<span id="sent15">Poco después de que Mario naciera, su padre reveló que tenía una relación con una mujer alemana y, como resultado de dicha unión, el escritor tiene dos medio hermanos menores: Enrique y Ernesto Vargas.<br /><br /></span>

	<span id="sent16">Vargas Llosa vivió con su familia materna en Arequipa hasta un año después del divorcio de sus padres, momento en que su abuelo fue nombrado cónsul honorario del Perú en Bolivia. </span>

	<span id="sent17">Con su madre y la familia de esta, Vargas Llosa se muda a Cochabamba, Bolivia, donde pasó los primeros años de su niñez. </span>

	<span id="sent18">Los Llosa eran mantenidos por su abuelo, quien administraba una plantación de algodón. </span>

	<span id="sent19">Siendo niño, a Vargas Llosa se le hizo creer que su padre había fallecido, ya que su madre y su familia no querían explicarle que se habían separado. </span>

	<span id="sent20">Durante el gobierno del presidente José Luis Bustamante y Rivero, su abuelo obtuvo un cargo diplomático en la ciudad de Piura, en parte porque eran parientes, por lo que la familia entera regresó al Perú. </span>

	<span id="sent21">En Piura, Vargas Llosa cursó la escuela primaria en el Colegio Salesiano Don Bosco y su último año de secundaria en el Colegio San Miguel de Piura.<br /><br /></span>

