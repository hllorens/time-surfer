	<span id="sent25">La relación con su padre, siempre tortuosa, marcaría el resto de su vida. </span>

	<span id="sent26">Por años, guardó hacia él sentimientos entremezclados, como el temor y el resentimiento, debido a que durante su niñez debió soportar violentos arrebatos de parte de su padre, además de un resentimiento hacia la familia Llosa y grandes celos para con su madre; pero, sobre todo, a causa de la repulsión de su padre hacia su vocación literaria, que nunca llegó a comprender.<br /><br /></span>

