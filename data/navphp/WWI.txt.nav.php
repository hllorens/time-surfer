	<span id="sent505">Technology.<br /><br /></span>

	<span id="sent506">The First World War began as a clash of twentieth century technology and nineteenth century tactics, with inevitably large casualties. </span>

	<span id="sent507">By the end of 1917, however, the major armies, now numbering millions of men, had modernized and were making use of telephone, wireless communication, armoured cars, tanks, and aircraft. </span>

	<span id="sent508">Infantry formations were reorganized, so that 100 man companies were no longer the main unit of maneuver. </span>

	<span id="sent509">Instead, squads of 10 or so men, under the command of a junior NCO, were favoured. </span>

	<span id="sent510">Artillery also underwent a revolution.<br /><br /></span>

	<span id="sent511">In 1914, cannons were positioned in the front line and fired directly at their targets. </span>

	<span id="sent512">By 1917, indirect fire with guns (as well as mortars and even machine guns) was commonplace, using new techniques for spotting and ranging, notably aircraft and the often overlooked field telephone. </span>

	<span id="sent513">Counter-battery missions became commonplace, also, and sound detection was used to locate enemy batteries.<br /><br /></span>

	<span id="sent514">Germany was far ahead of the Allies in utilising heavy indirect fire. </span>

	<span id="sent515">She employed 150 and 210 mm howitzers in 1914 when the typical French and British guns were only 75 and 105 mm. </span>

	<span id="sent516">The British had a 6 inch (152 mm) howitzer, but it was so heavy it had to be hauled to the field in pieces and assembled. </span>

	<span id="sent517">Germans also fielded Austrian 305 mm and 420 mm guns, and already by the beginning of the war had inventories of various calibers of Minenwerfer ideally suited for trench warfare.<br /><br /></span>

	<span id="sent518">Much of the combat involved trench warfare, where hundreds often died for each yard gained. </span>

	<span id="sent519">Many of the deadliest battles in history occurred during the First World War. </span>

	<span id="sent520">Such battles include Ypres, the Marne, Cambrai, the Somme, Verdun, and Gallipoli. </span>

	<span id="sent521">The Haber process of nitrogen fixation was employed to provide the German forces with a constant supply of gunpowder, in the face of British naval blockade. </span>

	<span id="sent522">Artillery was responsible for the largest number of casualties and consumed vast quantities of explosives. </span>

	<span id="sent523">The large number of head-wounds caused by exploding shells and fragmentation forced the combatant nations to develop the modern steel helmet, led by the French, who introduced the Adrian helmet in 1915. </span>

	<span id="sent524">It was quickly followed by the Brodie helmet, worn by British Imperial and U.S. troops, and in 1916 by the distinctive German Stahlhelm, a design, with improvements, still in use today.<br /><br /></span>

	<span id="sent525">The widespread use of chemical warfare was a distinguishing feature of the conflict. </span>

	<span id="sent526">Gases used included chlorine, mustard gas and phosgene. </span>

	<span id="sent527">Few war casualties were caused by gas, as effective countermeasures to gas attacks were quickly created, such as gas masks. </span>

	<span id="sent528">The use of chemical warfare and small-scale strategic bombing were both outlawed by the 1907 Hague Conventions, and both proved to be of limited effectiveness, though they captured the public imagination.<br /><br /></span>

	<span id="sent529">The most powerful land-based weapons were railway guns weighing hundreds of tons apiece. </span>

	<span id="sent530">These were nicknamed Big Berthas, even though the namesake was not a railway gun. </span>

	<span id="sent531">Germany developed the Paris Gun, able to bombard Paris from over 100 kilometres (60 mi), though shells were relatively light at 94 kilograms (210 lb). </span>

	<span id="sent532">While the Allies had railway guns, German models severely out-ranged and out-classed them.<br /><br /></span>

	<span id="sent533">Fixed-wing aircraft were first used militarily by the Italians in Libya 23 October 1911 during the Italo-Turkish War for reconnaissance, soon followed by the dropping of grenades and aerial photography the next year. </span>

	<span id="sent534">By 1914 the military utility was obvious. </span>

	<span id="sent535">They were initially used for reconnaissance and ground attack. </span>

	<span id="sent536">To shoot down enemy planes, anti-aircraft guns and fighter aircraft were developed. </span>

	<span id="sent537">Strategic bombers were created, principally by the Germans and British, though the former used Zeppelins as well. </span>

	<span id="sent538">Towards the end of the conflict, aircraft carriers were used for the first time, with HMS Furious launching Sopwith Camels in a raid to destroy the Zeppelin hangars at Tondern in 1918.<br /><br /></span>

	<span id="sent539">German U-boats ( submarines) were deployed after the war began. </span>

