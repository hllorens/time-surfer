	<span id="sent565">Genocide and ethnic cleansing.<br /><br /></span>

	<span id="sent566">Ottoman Empire.<br /><br /></span>

	<span id="sent567">The ethnic cleansing of the Ottoman Empire's Christian population, with the most prominent among them being the deportation and massacres of Armenians (similar policies were enacted against the Assyrians and Ottoman Greeks) during the final years of the Ottoman Empire is considered genocide. </span>

