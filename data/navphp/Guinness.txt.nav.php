	<span id="sent105">Sinking bubbles.<br /><br /></span>

	<span id="sent106">A long time subject of bar conversations is the Guinness cascade, where the gas bubbles appear to travel downwards in a pint glass of Guinness.<br /><br /></span>

	<span id="sent107">The effect is attributed to drag; bubbles that touch the walls of a glass are slowed in their travel upwards. </span>

	<span id="sent108">Bubbles in the centre of the glass are, however, free to rise to the surface, and thus form a rising column of bubbles. </span>

	<span id="sent109">The rising bubbles create a current by the entrainment of the surrounding fluid. </span>

	<span id="sent110">As beer rises in the centre, the beer near the outside of the glass falls. </span>

	<span id="sent111">This downward flow pushes the bubbles near the glass towards the bottom. </span>

	<span id="sent112">Although the effect occurs in any liquid, it is particularly noticeable in any dark nitrogen stout, as the drink combines dark-coloured liquid and light-coloured bubbles.<br /><br /></span>

