	<span id="sent49">Composition.<br /><br /></span>

	<span id="sent50">Guinness stout is made from water, barley, hops, and brewer's yeast, and is treated with isinglass finings made from fishes' air bladders, although Guinness has claimed that this finings material is unlikely to remain in the finished product. </span>

	<span id="sent51">A portion of the barley is roasted to give Guinness its dark colour and characteristic taste. </span>

