	<span id="sent57">Nitrogen is less soluble than carbon dioxide, which allows the beer to be put under high pressure without making it fizzy. </span>

	<span id="sent58">The high pressure of dissolved gas is required to enable very small bubbles to be formed by forcing the draught beer through fine holes in a plate in the tap, which causes the characteristic ``surge'' (the widget in cans and bottles achieves the same effect). </span>

	<span id="sent59">The perceived smoothness of draught Guinness is due to its low level of carbon dioxide and the creaminess of the head caused by the very fine bubbles that arise from the use of nitrogen and the dispensing method described above. </span>

